# 附加食物中毒保险条款

**总 则**

**第一条** 投保人只有在投保了《旅游者人身意外伤害险条款》等旅游类险种（以下简称 为“主险”）后，方可投保本附加条款（以下简称为“本附加条款”）。

**第二条** 本附加条款与主保险合同条款不一致之处，以本附加条款为准；本附加条款 未尽之处，以主保险合同条款为准。

**第三条** 本附加条款所附属的主保险合同效力终止，本附加条款效力即行终止。主保险合同无效，本附加条款亦无效。

 

**保险责任**

**第四条** 保险责任 

在保险期间内，被保险人因食物中毒的身故、残疾，或在医院接受治疗的，保险人将参照保险合同项下相关保险事故的条款约定，承担相应保险金给付责任。 

 

**保险金额和保险费**

**第五条** 保险金额 

保险金额是保险人对被保险人每次旅行承担给付保险金责任的最高限额。保险金额由投 保人、保险人双方约定，并在保险单中载明。 

**第六条** 保险费 

投保人应该按照合同约定向保险人交纳保险费。保险人和投保人可以在本保险合同项下 约定免赔额等限制条件。 

 

**释 义**

**1****、食物中毒：**指因进食了受污染或受感染了的食物而导致的食物中毒。


## 附件四 营探服务商订单管理规范

1. 活动发布规则

1.1 保险

1.1.1服务商须具有投保有效的组织者责任险。若服务商选择通过营探合作的第三方保险公司投保组织者责任险，须如实提供/填写相关信息用于保险公司进行核保。如组织者责任险通过其他机构自行购买，服务商须确保活动开始时，保险已生效，因保险未生效所产生的一切损失由服务商自行承担；

1.1.2场地责任险中如包含组织者责任的部分，且该组织者责任部分可以覆盖活动全过程，则可以视为服务商具有组织者责任险；

1.1.3出行人不符合要求的，组织者责任险可能不予承保，服务商应当对此类出行人员健康情况进行提前了解，建议其提供健康说明并督促其购买人身保险。

1.2 库存

1.2.1服务商须合理设置库存数量，库存数量为活动在本平台的可报名人数，小于或等于活动总人数，大于或等于最低成团人数；

1.2.2超卖是由于服务器在同一时间遭受到超量报名所形成的偶发现象，是不可避免产生的情形，服务商须理解并同意2%数量的超卖现象存在；当超卖情形出现，服务商应当积极配合平台处理售后。

1.3 活动日期

对于仅购买场地责任险的商品，服务商可选择是否在活动开始当日可售。如服务商选择当日可售，可能存在当日保险尚未生效的情形，服务商承诺了解并知悉，保险合同生效日期以保险单所载生效日期为准。

1.4 服务商不得刻意发布规避信息或重复发布：同一活动时间/期的同一款商品不得多次提交发布，不得通过修改关键属性使同一款商品成为另一款商品发布。

1.5商品取消规则

1.5.1商品未上架

服务商在商品未上架期间，可以通过工作台申请取消商品发布且无需承担对消费者的责任。

1.5.2商品上架后

服务商在商品审核通过上架后，服务商可以通过工作台申请取消活动，平台将根据该商品是否存在订单、是否成团、取消原因等不同情形进行处理。如果该商品不存在订单，服务商可取消该活动；如果该商品存在订单但为未成团商品，服务商可取消该活动且无需承担对消费者的违约责任；如果该商品存在订单且已成团，服务商取消活动，应根据双方约定向消费者承担相应的违约责任，并承担平台相应处罚措施（包括但不限于：警告、排序降权、降低信用评价、取消优惠及福利政策、收取罚金等），因不可抗力和特殊原因的除外。

1.5.3 如因服务商取消活动导致已审核通过的订单被取消，服务商应按照商品详情页面与消费者的约定承担相应的违约责任，或与被取消订单的消费者主动协商达成退赔方案；如发生交易争议，营探将按照平台规定处理。

1.5.4 对于因服务商取消活动而导致交易不成功的订单，平台将依据《服务商收费标准》中的必要标准收取平台服务费。

1.6开营须知

服务商有义务在商品发布时，详细填写开营须知部分，以提示出行人在活动中可能面临的风险，并对出行人进行安全教育。

2. 订单审核规范

2.1服务商对消费者通过平台提交的订单负有审核义务，在消费者提交订单（以营探平台收到消费者已成功支付订单的时间为准）后，平台将通过站内信、短信或邮件的方式通知服务商，服务商应自收到通知起的48小时之内对订单进行审核，判断是否对消费者的要约进行承诺。

2.1.1若订单审核通过，该订单生效、合同成立。若订单审核不通过，服务商应当及时、如实填写不通过理由，该原因会自动通知消费者，平台将原路退回消费者所支付费用至消费者支付账户。如未及时审核，所导致的缔约过失责任及平台的违规处罚措施由服务商承担。 

2.1.2对于服务商未及时审核通过的订单，营探审核系统将默认订单审核通过。对于系统默认审核通过的订单，服务商应当承担与自主操作审核的订单相同的责任。

2.2 服务商对订单的审核内容包括：

2.2.1出行人基本信息（年龄等）是否符合出行条件；

2.2.2联系人信息（联络人姓名、电话等）是否有遗漏；

2.2.3是否涉及同一出行人重复报名同一时段的同一个产品；

2.2.4确认订单金额/优惠扣除是否正确；

2.2.5是否符合商品详情页所体现的其他出行要求。

2.3对于因服务商审核不通过而导致交易不成功的订单，平台将依据《服务商收费标准》中的必要标准收取平台服务费。 

2.4服务商未在规定时间内审核及在规定审核时间内不通过订单的比例超过该商品总订单数量的10%，基于消费者合法权益及平台声誉的保护，平台将对该服务商采取处罚措施，包括但不限于：警告、排序降权、降低信用评价、暂停售卖、取消优惠及福利政策、收取罚金等。服务商主张订单不通过系因出行人不符合报名限制的，应及时向平台申诉并按照要求提交申诉材料。

2.5自订单生效后至出行前，消费者可以联系平台客服就订单内容进行修改，平台客服将修改后的订单提交给服务商，由服务商对订单的内容进行再次审核。

3. 行程管理规范

3.1 客服

3.1.1服务商应配备充足、专业的客服人员与消费者沟通，处理来自消费者的问题，包括但不限于活动的售前、售中和售后服务；

3.1.2服务商客服应及时响应消费者的咨询，维护平台和服务商的良好形象；

3.1.3服务商或平台邀请已报名消费者和出行人进入企业微信群；

3.1.4服务商不得通过平台客服工具和活动企业微信群发布非活动信息，包括但不限于非营探平台的广告商品信息、未经营探许可的第三方链接；

3.1.5服务商不得通过平台客服工具和活动企业微信群将营探用户引流至其他平台、不得进行销售行为，不得将平台客服工具和活动企业微信群用于任何为营探用户提供客户服务之外的目的。

3.1.6服务商客服在沟通过程中应遵守国家法律法规、服务商与平台的约定及平台发布的相关规则，不得发送涉及反动、色情、威胁、恐吓、暴力、传销的内容，不得诋毁营探平台形象或其他服务商、商品和活动的形象，不得扰乱营探平台的运营及管理秩序；

3.1.7服务商客服不得在沟通过程中使用任何形式的有人身攻击、侮辱性等不文明的语言，包括但不限于诽谤、骚扰、诋毁、谩骂出行人以及使用任何引起出行人不满的字句或以其他方式侵犯出行人的合法权益的行为；

3.1.8服务商客服应对在服务过程中所收集的出行人个人信息予以严格保密，要采取相关必要措施防止相关个人信息泄露和丢失；

3.1.9服务商客服须服从社群管理，未尽事项详见《营探社群管理规定》。

3.2 住宿

3.2.1服务商应充分做好活动住宿的安全保障工作；

3.2.2活动住宿应有导师全程值班，负责现场管理、意外事件处理等；

3.2.3活动住宿应分性别分配住宿，男女不混宿，住宿须由同性别导师管理、由同性别导师夜间巡逻，异性导师不能进入营员宿舍；

3.3 应急事件的处理

3.3.1服务商应当制定安全应急预案，防止各类安全事故的发生；

3.3.2服务商应充分履行告知义务，按照规定提前告知风险，做出对应的安全提示和培训，及时就可能危及出行人人身、财物安全的情况，向出行人作出真实说明和明确警示；

3.3.2出行人在途中发生意外事故，服务商应当立即采取必要的救助和处置措施，依法履行报告义务，并对出行人作出妥善安排。




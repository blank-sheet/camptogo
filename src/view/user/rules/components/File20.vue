## 附件六：服务商违规行为管理总则

 第一章 概述 

1.1 目的及依据

为给营探平台内服务商及消费者，营造一个良好的交易环境，保护消费者的合法权益，确保服务商更好更规范地在营探平台开展经营活动，根据国家现行法律、法规、规章、服务商管理办法，特修订《服务商违规行为管理规则》。

1.2 适用范围

全部服务商。

1.3 效力级别

特别规定与一般规定不一致的，适用特别规定；新的规定与旧的规定不一致的，适用新的规定。营探平台规则尚无规定的，根据法律规定或相关协议处理。

 第二章 违规处理流程 

2.1 违规判定 

营探平台基于大数据主动排查、订单抽检、行政管理部门的通报/通知、司法机关的法律文书、消费者投诉、权利人举报、新闻媒体曝光、及其他合法合规渠道对经营行为进行违规判定。

2.2 行为纠正

营探平台对服务商的违规行为先进行纠正，消除不利影响，包括但不限于封禁相关商品、追回/取消违规所得利益等。违规行为对消费者造成损失的，服务商需依据营探交易争议处理规则，对消费者进行合理赔偿。如对营探平台造成损失，应赔偿因此给营探平台造成的损失。

同时，服务商的违法、违规等情况受到国家相关监管部门的调查、处理。营探平台有权要求服务商向监管机关提供相关资料，责令服务商予以整改、执行监管意见等，并保留进一步追究服务商法律责任的权利。

2.3 违规处理

营探平台将对判定后的违规行为进行处理，并通过消息中心对服务商的违规处理结果进行通知。

2.4 申诉处理

服务商若不认可该违规处理，可在收到违规处理通知的七（7）日内发起违规申诉，未在申诉时效内进行申诉处理的，视为服务商认可违规处理。

 第三章 处理措施 

营探平台根据违规行为的情节严重程度，可采取包括但不限于以下处理措施：服务商若违反本规则相关规定，营探除依照营探规则进行处理外，营探有权依据本规则进行处理，处理措施有如下几种形式：警告；扣除诚信保证金；删除违规信息、删除作弊数据；增加营探平台服务费；禁止享受营探平台优惠政策；屏蔽商品链接、屏蔽服务商链接；商品下架、服务商下架；账户冻结（暂停结算）；账户清退。若服务商多次违规后拒不整改、多条并犯，或对营探平台造成恶劣影响，营探平台有权加重处理。

（1）警告

即在营探平台的消息中心、规则中心、奖惩中心等页面，对服务商的不当行为进行提醒告诫，或对被执行的处理进行公示。

（2）扣除诚信保证金

根据服务商的违规严重程度，从服务商的货款/保证金账户中，扣除一定金额的违约金。如服务商签署划扣确认书，将优先从货款中扣除。

（3）限制服务商权限


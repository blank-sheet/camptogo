3.3.3 危及消费者权益

【违规行为描述】 危及消费者权益，即服务商的商品、服务或交易模式出现大量导致消费者权益受损的情况，或有导致消费者权益受损的趋势。

【违规处理】

相关产品予以下架，此外，

情节轻微，每出现一次违规，扣除50元；

情节一般，每出现一次违规，扣除100元；

情节严重，每出现一次违规，扣除200元；

情节特别严重，每出现一次违规，扣除400元；

若服务商多次违规且未予整改的，营探平台有权限制服务商发布商品。

 如何理解 “情节轻微”、 “情节一般”、“情节严重”“情节特别严重”？ 

（1）情节轻微：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验，或对营探平台造成不良影响，但未造成消费者资金损失。

（2）情节一般：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验，或对营探平台造成不良影响，并可能导致消费者产生资金损失。

（3）情节严重：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验，或对营探平台造成不良影响，并导致消费者产生部分资金损失。

（4）情节特别严重：

服务商的相关行为，已对消费者造成了不良体验，或对营探平台造成不良影响，并导致消费者产生严重资金损失。

3.3.4 违背交易流程

【违规行为描述】违背交易流程， 指服务商在交易过程中，未按正常交易流程进行交易的违规行为。

包括但不限于以下几种行为：

1.1 消费者取消订单后，服务商仍强迫消费者接受交易；

1.2 服务商无故或未与消费者协商一致取消订单；

1.3 服务商无故或未与消费者协商一致修改订单信息；

1.4 服务商诱导消费者撤销投诉、取消退款申请、修改评价、确认收货；

1.5 服务商以各种方式诱导消费者脱离营探平台进行交易，包括但不限于下列情形：

服务商以非正常交易为目的，与消费者通过非营探平台提供的渠道进行交易，侵害消费者权益，包括但不限于：提供或使用银行账号、支付二维码等第三方支付工具以及第三方购物链接等；或引导消费者在营探平台支付部分款项后，脱离营探平台收取剩余款项；

1.6 服务商无故或未经消费者同意前私自核销电子凭证,包括但不限于下列情形：

旅游/酒店/门票/交通类等使用电子凭证的交易，(除买卖家双方预约出行/入住时间的场景)，服务商以赠品、优惠、套餐升级等方式诱导消费者提供券码、券号等相关电子凭证，或通过券码营探平台等其他手段获取或查询未使用的核销凭证。

【违规处理】 

相关产品予以下架，此外，

情节轻微，每出现一次违规，扣除50元；

情节一般，每出现一次违规，扣除100元；

情节严重，每出现一次违规，扣除200元；

情节特别严重，每出现一次违规，扣除400元；

若服务商多次违规且未予整改的，营探平台有权限制服务商发布商品。

 如何理解 “情节轻微”、 “情节一般”、“情节严重”“情节特别严重”？ 

（1）情节轻微：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验；或对营探平台造成不良影响，但未造成消费者资金损失；

（2）情节一般：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验，或对营探平台造成不良影响，并可能导致消费者产生资金损失。

（3）情节严重：

服务商的相关行为，营探平台认定该行为对消费者造成了不良体验；或对营探平台造成不良影响，并可能导致消费者产生资金损失； 

（4）情节特别严重：

有大量订单出现违背交易流程行为；或多次出现违背交易流程行为。

3.3.5 不当获取/使用信息

【违规行为描述】指通过租借/共享账号、协助第三方扫描系统等方式获取营探平台商业信息/他人信息，或未经允许发布、传递、出售营探平台商业信息/他人信息，致使营探平台商业信息/他人信息存在泄露风险的行为。

【违规处理】

情节一般，每出现一次违规，扣除100元；

情节严重，每出现一次违规，扣除200元；

情节特别严重，每出现一次违规，扣除400元；

若服务商多次违规且未予整改的，营探平台有权限制服务商发布商品。

 如何理解 “情节一般”、“情节严重”“情节特别严重”？ 

（1）情节一般：

不当获取使用营探平台商业信息、或未妥善保存信息、存在泄露风险；

服务商因在账号管理、内部管理、合作伙伴管理、系统安全等方面存在过失而导致他人个别信息泄露；

（2）情节严重：

未经允许对外披露、共享他人信息的，如服务商未经允许向非消费者本人披露消费者订单信息、在评价页面未经允许披露他人信息等；

不当获取使用营探平台商业信息、或未妥善保存信息、由此引发泄露事件；

（3）情节特别严重：

服务商不当获取使用信息手段恶劣的；

服务商不当获取使用信息量级巨大、造成严重后果、被追究或可能追究刑事责任等情形的；

3.3.6 不当获利 

【违规行为描述】服务商通过服务商及其关联服务商，有组织地规避营探平台规则、协议或法律法规，试图或已经取得不合法利益，侵害消费者利益，对营探平台造成不良影响的行为。


#  营探服务商管理办法

发布日期：2023年4月27日

生效日期：2023年5月4日

## 第一章 总则

 1.1 目的

为维护正常运营秩序，建立健康规范的电子商务平台，保障营探消费者合法权益，营探平台（以下简称我们或营探或平台）制定本管理办法。

 1.2 适用范围

本规则适用于成为营探用户后，依据平台要求申请入驻营探并通过审核，在营探发布商品的自然人、法人和非法人组织（以下简称服务商）。

## 第二章 开通账户及经营规范

 2.1 开通条件

服务商申请在本平台入驻，须满足 《营探服务商入驻协议及授权条款》 的相关要求，并同意遵守营探平台的各项协议及规定。申请注册服务商账户，须先使用服务商的授权用户信息注册营探用户，并完成实名认证。之后通过招商平台或邮件的方式向营探提交入驻申请材料。通过入驻审核的，营探将为其开通服务商账户。同一个身份信息只允许申请开通一个服务商账户。

 2.2 服务商准入及核验

服务商提交入驻申请材料，经营探审核通过后与其签订相关入驻协议，并为服务商开通营探服务商工作台账户。如服务商提供的相应证照、资质证件已过有效期，则服务商有义务及时提供更新后的证照和证件，否则营探有权暂停或关闭服务商账户权限。

2.2.1核验更新

营探平台每六个月（自上次服务商基本信息审核通过的当日起180天）对所有入驻服务商的资质、证照等基本信息进行核验更新。

营探将在六个月期限届满前一周，以站内信或邮件的形式通知服务商提交基本信息审核，服务商应当及时查看基本信息是否符合当前经营状况，如有不符应当及时修改并再次提交审核，如无不符可直接提交审核。若在六个月期限届满当日仍未通过基本信息审核，部分功能将被冻结，服务商提交基本信息并审核通过后方可恢复正常使用。

 2.3 系统程序

服务商均须遵守营探平台系统现行的各项程序、流程及规则，共同维护平台系统正常运营秩序。如违反或者规避平台系统各项程序、流程及规则，则营探有权暂停或者终止提供相应服务。系统程序、流程及规则的设置不得限制服务商的自主选择权。

 2.4 入驻经营

2.4.1营探作为营探平台运营者未授权任何第三方机构代理招商，申请入驻流程及相关规则均以营探平台官方招商页面为准；

2.4.2营探有权根据，包括但不限于商品和服务内容、资源资质、资源需求、公司经营状况、服务水平、管理水平等因素退回服务商的入驻申请，同时有权在申请入驻及后续经营阶段要求服务商提供其他资质证明文件；

2.4.3 营探将结合国家法律法规、行业动态的要求，不定期更新入驻标准；服务商须符合营探最新的入驻标准，并根据该标准的具体规定补充、更新文件；

2.4.4申请入驻的服务商必须如实提供资料和信息，并确保申请入驻及后续经营阶段提供的相关资质和信息的材料合法、真实、有效、准确、清晰（若您提供的相关资质为其他公司/个人所有，如商标权、著作权等，须提供合法有效的授权文件，并先行自行核实该授权行为的有效性）；一旦发现虚假资质或信息的，营探有权根据营探规则及与服务商签署的相关协议之约定进行处理。

 2.5 信息发布规范

服务商除须遵守 《营探用户协议》、《营探隐私政策》 等营探平台规则中关于信息发布的相关规定外，还须遵循如下信息发布要求：

2.5.1服务商不得发布具有危害性的信息，包括但不限于反动等破坏性信息、色情低俗信息；

2.5.2服务商不得发布虚假宣传信息，包括但不限于捏造细节、图文不符、夸大事实、虚假中奖信息、伪造官方活动的信息；

2.5.3服务商不得发布不当信息，包括但不限于发布其他机构信息、垃圾广告和大量重复内容，同时服务商通过营探推广时须遵守营探相关推广规则；

2.5.4服务商不得发布营探不允许发布的商品和服务信息，详见【附件一 营探服务商信息发布规范】、【附件二 营探平台禁发信息规范】。

2.5.5服务商不得出售国家法律法规或营探管理要求禁止销售的商品和服务。服务商发布具体商品和服务内容信息详见平台规则；

2.5.6服务商发布不遵循以上要求的，营探有权对禁发内容采取删除、拦截发表等处理措施。

详见【 附件一  营探服务商信息发布规范 】 、【附件二  营探平台禁发信息规范】。 

 2.6订单管理规范 

2.6.1商品详情页仅为要约邀请，消费者在营探预订商品并支付成功为向服务商发出要约，服务商对订单审核确认通过为承诺，此时该订单生效，合同成立。

2.6.2服务商对消费者通过平台提交的订单负有审核义务，在消费者提交订单（以营探平台收到消费者已成功支付订单的时间为准）后，平台将通过站内信、短信或邮件的方式通知服务商，服务商应自收到通知起的48小时之内对订单进行审核，判断是否对消费者的要约进行承诺。若订单审核不通过，服务商应当及时、如实填写不通过理由，平台将原路退回消费者所支付费用至消费者支付账户。

2.6.3对于服务商未及时审核的订单，营探系统将默认该订单的审核结果为通过。对于系统默认审核通过的订单，服务商应当承担与自主操作审核的订单相同的责任。

2.6.4如因服务商取消活动导致已审核通过的订单被取消，服务商应按照商品详情页面与消费者的约定承担相应的违约责任，或与被取消订单的消费者主动协商达成退赔方案；如发生交易争议，营探将按照平台规定处理。

2.6.5对于因服务商审核不通过或取消活动而导致交易不成功的订单，平台将依据《服务商收费标准》中的必要标准收取平台服务费。

2.6.6服务商未在规定时间内审核及在规定审核时间内不通过订单的比例超过该商品总订单数量的10%，基于消费者合法权益及平台声誉的保护，平台将对该服务商采取处罚措施，包括但不限于：警告、排序降权、降低信用评价、暂停售卖、取消优惠及福利政策、收取罚金等。服务商主张订单不通过系因出行人不符合报名限制的，应及时向平台申诉并按照要求提交申诉材料。

2.6.7服务商对消费者通过平台提交的订单负有管理义务，包括但不限于：及时为消费者提供售前咨询及售后服务。

2.6.8 具体规定见 《营探服务商管理办法》 【 附件四 】**营探服务商订单管理规范。**

 2.7 行为规范

2.7.1服务商不得违背其自身所作出的服务承诺；

2.7.2服务商不得存在违规交易行为，包括但不限于引导消费者进行线下交易；

2.7.3服务商不得出现作弊行为，包括但不限于通过不正当方式获得关注数、点赞、评论、转发或进行内容抄袭、通过不正当、作弊手段获得平台权益、权限等；

2.7.4服务商不得推广假冒、盗版、山寨、不安全、不环保等的商品和服务；

2.7.5服务商不得出现提交虚假材料行为，包括但不限于入驻申请、商品和服务信息发布材料、违规申诉等环节的资料提交；

2.7.6服务商不得有其他侵犯他人权益的行为，包括但不仅限于泄露他人隐私信息、不当使用他人权利、骚扰他人；

2.7.7服务商不得未经营探书面同意向其他任一公司/个人泄漏服务商和营探的任意数据、信息等商业秘密；

2.7.8服务商不得利用平台权益进行金钱交易或变相收取利益等行为。

 2.8 商品和服务价格规范 

2.8.1定义

（1）划线价格：划线是将商品价格用横线划掉的标注方式；划线价格可能是商品的正品零售价、商家指导价或该商品曾经展示过的销售价格，并非原价，仅供参考；由于地时间的差异性和市场行情的波动，该价格可能与您预订时展示的不一致。

（2）未划线价格：指商品的实时标价，并非成交价格；未划线价格不因表述的差异改变性质，具体的成交价格根据商品参加优惠活动，或使用优惠券等发生变化，最终以订单结算页价格为准。（商家以图片或文字形式标注的一口价、促销价、优惠价等价格：可能是在使用优惠券或参加特定优惠活动情形下的价格，具体请以结算页面的标价、优惠条件或活动规则为准。）

（3）价格欺诈：指服务商利用虚假或者使人误解的价格手段，诱骗消费者或者第三方与其进行交易的行为。

2.8.2定价规范

（1）服务商承诺通过营探平台发布的商品的价格 不高于该服务商同款商品在其他渠道的销售价格 （包括服务商其门店线下销售的价格、其官方在线销售渠道的销售价格，该销售价格指不包含任何优惠折扣的原价），若通过营探平台发布的商品的价格高于上述价格，本平台将要求服务商立刻返还差价给平台消费者并下调平台展示的商品价格。未经营探允许服务商不得私自进行篡改价格等扰乱市场价格的促销售卖行为。

（2）适用政府指导价或政府定价的商品，服务商设置的价格须执行政府指导价或政府定价，不得改动或者超出浮动幅度。

（3）商品价格描述应真实准确，并符合事实，售价一律以营探前端页面实际交易价格为准。

（4）服务商在参加营探的促销活动前，不得随意上调商品价格，市场价格浮动情形除外，但须以营探核实情况为准。如服务商违反本条规定，营探有权限制服务商参加营探的促销活动。

（5）服务商其他标价行为

a.服务商采用与其他经营者或者其他销售业态进行价格比较的方式开展促销活动，应当准确标明被比较价格的含义，且能够证明标示的被比较价格真实有依据；

b.服务商采取馈赠物品或者服务等方式开展促销活动，馈赠物品或者服务须标示价格（价值），且标示价格（价值）应当真实明确；

c.服务商采取返还优惠券等方式开展促销活动，若附加使用条件，应以显著形式标示的。

2.8.3价格违规行为

服务商须严格遵守 《中华人民共和国价格法》、《中华人民共和国反不正当竞争法》、《禁止价格欺诈行为的规定》 等相关法律法规，遵循市场规律，确保可以提供任一价格的合法依据或可供比较的出处，不得出现任何欺骗性价格标识以及涉嫌价格欺诈的行为。

2.8.4价格违规管理

（1）若发现商品价格不合规范时，营探有权直接将相应违规商品下架，要求服务商进行整改，整改后经营探审核可重新上架。

（2） 营探会不定时对服务商标注的价格进行抽查监督，如发现价格标注违规，则服务商应承担相应违约责任，并且营探将采取包括但不限于以下市场管控措施：限制发布商品、公示警告、屏蔽等。

 2.9 营销推广规范

2.9.1营销活动价格须遵循本办法 【  2.7行为规范】 、【 附件一  营探服务商信息发布规范】 及其他营探规则中的相关要求；

2.9.2营销活动广告语及商品和服务描述规范

如果服务商不具有相关资质或未参加营探的相关活动，不允许在标题及广告语中使用与特定资质或者活动相关的词汇：

（1）商品和服务描述：不得出现不对称的促销信息；

（2）营销活动链接图：不得出现图片上描述的促销时间已经过期，或者图片上描述的信息和点击进入商品和服务详情页后的信息不一致；

（3）服务商在经营活动和产品描述及宣传中，其内容应当提供真实、合法、准确客观的信息，不得使用含糊、易引起误解的语言、文字、图片或影像，不得以保留最终解释权为由，损害消费者的合法权益；

（4）营销活动商品和服务须符合营探产品发布规范，包括【 附件一  营探服务商信息发布规范】、【附件二  营探平台禁发信息规范】。 

2.9.3营销活动中的优惠券与优惠促销费用承担，参见 《营探服务商收费标准》 。

 2.10 住宿地点规范

2.10.1住宿地点房间及公共区域应配备消防设施，如喷淋系统等；

2.10.2住宿地点房间及公共区域应配备火灾探测系统；

2.10.3住宿地点房间应有基本防盗系统；

2.10.4应具有应急指示灯和应急逃生通道；

2.10.5住宿地点应有安全撤退计划，并在门上用图表标示；

2.10.6住宿地点应配备24小时保安人员。

## 第三章 违规处理

 3.1 服务商若违反本规则相关规定，营探除依照营探规则进行处理外，有权依据本规则进行处理，处理措施有如下几种形式：

3.1.1警告；

3.1.2删除违规信息、删除作弊数据；

3.1.3扣除诚信保证金；

3.1.4增加平台服务费；

3.1.5禁止享受平台优惠政策；

3.1.6屏蔽商品链接、屏蔽服务商链接；

3.1.7商品下架、服务商下架；

3.1.8账户冻结（暂停结算）；

3.1.9账户清退。

具体违规内容及对应违规处理方式见：【 附件三《营探服务商违规处理及申诉材料一览表》】 。

 3.2 违规处理流程

3.2.1通知

（1）营探可通过消费者/权利人的投诉、举报、起诉、行政监管机关的检查或平台检查等途径发现服务商的违规行为；

（2）营探发现服务商存在违规行为后，将汇总服务商违规情况，通过服务商向营探提供的有效联系方式向服务商发送违规消息通知；

（3）如经营探核实，确认服务商违规行为已达到处罚标准，将向服务商发送违规处罚通知，同时在服务商工作台对违规情况进行公示；

（4）营探将视服务商违规行为的严重程度，选择是否向消费者进行违规公示；

（5）公示附件。

3.2.2申诉

申诉服务商在收到营探的违规通知后，如存在异议，须在 三（  3）个工作日 内通过向指定邮箱发送申诉声明的方式提出申诉，并一同提交相关证明材料，营探会对申诉理由进行核实后反馈申诉结果。

3.2.3申诉结果及处罚

（1）如服务商在申诉有效期内发起申诉且申诉成功，营探将不会认定服务商此次违规行为成立，且不会对服务商采取任何处罚措施；

（2）如服务商在有效内未发起申诉，或申诉失败，营探将认定服务商此次违规行为成立，并对服务商采取相应的处罚措施；

（3）如服务商在一次违规事件中涉及多项违规内容，营探有权根据实际情况叠加或加大对服务商的惩罚力度；

（4）如服务商多次做出同一类违规行为，营探有权根据情节严重性，在原有的违规处罚措施基础上，加大对服务商的处罚。

## 第四章 违规申诉

 4.1 违规申诉流程

4.1.1服务商须在违规处理作出之日起 三（  3）个工作日 内对违约处理进行确认或发起违规申诉，逾期未进行上述操作的，本平台视为服务商认可违规处理，对应的违规处理将自动生效；

4.1.2服务商可通过 邮件方式向指定邮箱: partner@camptogo.com 发起违规申诉；

4.1.3针对同一项违规处理，服务商只有一次发起申诉的机会；


## **第三章 诚信保证金的调整**

**3.1**营探有权根据服务商的业务变化、经营类目、运营情况、违规情况及实际赔付情况等以**书面形式**通知服务商调整诚信保证金金额。

**3.2**服务商经营期间发生违反营探平台协议或平台规则情形的，营探有权根据平台协议及平台规则的规定提高其应缴诚信保证金金额。服务商出现违规情况或曾因违规被营探清退后再次申请加入，营探有权提高服务商的诚信保证金金额。

**3.3**如果服务商受到行政、司法机关调查或处罚等，营探有权提高服务商的诚信保证金金额。

**3.4**如果营探收到任何人对服务商发起的投诉、举报等，或者因服务商自身原因导致平台受到任何人发起的投诉、举报、索赔、诉讼或仲裁等，营探有权基于其诉求提高服务商应缴诚信保证金金额，增幅为营探因此可能遭受的全部损失或承担费用的总金额。

**3.5**服务商的关联方存在违法违规、违反平台协议、平台规则情形的，营探有权根据服务商在经营期间发生的违规行为、用户投诉举报、以及关联方违规记录等情况进行综合判断，并视情节严重程度提高其应缴诚信保证金金额。

**3.6**诚信保证金的具体调整金额由营探确定。

## **第四章 诚信保证金赔付与扣除**

**4.1**经营探审核认定服务商有下列情形之一的，营探有权根据实际情况从服务商已缴存的诚信保证金中扣除有关的款项用于对他人进行赔付、赔偿平台损失或支付其他应由服务商承担的款项或费用：

4.1.1服务商违反法律、法规、政策及营探相关规则导致用户和平台受损；

4.1.2服务商违反其对消费者的承诺导致用户和平台受损；

4.1.3未履行或延迟履行其商品和服务的承诺，致使用户受损和平台；

4.1.4提供的服务或商品质量不合格；

4.1.5虚假或夸大宣传；

4.1.6泄露他人隐私信息；

4.1.7侵犯他人知识产权、财产权、人身权利等权利；

4.1.8标错价影响消费用户活动；

4.1.9消费用户服务商的每一份有效投诉，营探将按该商品和服务金额的**300%**在诚信保证金中做出标识（每份有效投诉的费用扣除不少于500元）予以暂扣，有效投诉的判断，营探平台将按照一般标准进行确认；

4.1.10营探因服务商自身原因受到任何人发起的投诉、举报、索赔、诉讼或仲裁等，而遭受的全部损失或支出任何费用或款项；

4.1.11服务商扰乱营探平台秩序，通过不正当方式，刻意规避平台规则或市场管理措施行为，或以不正当的方式获取、使用平台资源的行为；

4.1.12服务商未落实环境保护责任，造成损害的；

4.1.13服务商在收到平台警告后，无视警告且拒不改正的；

4.1.14其他违反营探平台规则、与营探签订的协议或相关的服务承诺的情形。

**4.2** 营探提取服务商的诚信保证金进行任何赔付，均应以电子邮件、短信或营探通知、公告等书面形式向服务商发出，并说明赔付原因及赔付金额；在全部损失金额不明确之前，营探平台有权无限期扣留该服务商全部诚信保证金，直至全部损失金额明确后予以结算。

**4.3**诚信保证金不足的情形处理：

4.3.1当服务商的诚信保证金不足以赔付消费者、营探平台及其他权利人时，服务商应**自行支付**额外的赔付金额；如服务商在平台上有未结货款，平台有权从该款项中扣除以补足保证金；如货款不足以补足店铺保证金的，平台有权终止平台协议，停止服务。

4.3.2 服务商**有义务及时、足额缴存或补缴**相应的诚信保证金，否则平台有权停止向服务商提供部分或全部服务，直至服务商足额缴付相应诚信保证金；

4.3.3如服务商的诚信保证金不足以支付任何赔付金额，营探有权终止全部服务，包括但不限于采取冻结服务商应结交易款项、将服务商从平台清退以及追究服务商的违约责任；

4.3.4在服务商的诚信保证金不足时，营探没有义务使用自有资金向消费者或其他权利人支付赔偿金、补偿金或其他任何款项；

**4.4**如服务商违反有关的服务承诺和/或营探平台规则和/或服务商与营探签署的其他协议和/或营探认为对营探不利的其他情况，给营探造成的全部损失，营探有权从诚信保证金中扣除营探全部损失。

**4.5**若服务商在核算周期内实际领取的结算金额多于年度整体核算结果的，营探有权从服务商诚信保证金中扣除对应金额。扣除后，如诚信保证金金额不足，服务商应及时按约定保证金金额补足。

## **第五章 诚信保证金补缴**

**5.1**如诚信保证金可用余额不足，服务商应在收到补缴诚信保证金通知后的**三（3）个**工作日内及时向营探平台诚信保证金账户补缴足额的诚信保证金，使可用余额达至最低诚信保证金金额。

5.1.1当服务商的保证金余额低于应缴金额时，平台有权限制其部分功能的使用（包括但不限于货款提现、商品上架、订单审核等），待服务商补足保证金后功能恢复。

5.1.2营探可向机构发出补存通知，若服务商逾期未能补足诚信保证金，且服务商在营探平台仍有未结交易资金款项，营探有权从该等款项中扣除相应金额存入诚信保证金账户以补足诚信保证金。

5.1.3如服务商逾期未能补足诚信保证金，或者未结交易资金款项余额不足以补足诚信保证金的，则营探平台有权暂扣未结交易资金款项并暂停或终止服务。

**5.2**当服务当服务商申请退出（或被清退）营探平台时，须先按照营探平台规定补足诚信保证金至最低诚信保证金额度，如未按时补足，则营探有权自服务商未结交易资金款项中扣除相应金额资金予以补足诚信保证金，补足后服务商才可发起后续流程。

## **第六章 诚信保证金退回**

**6.1**若服务商申请退出（或被清退）营探平台，须在所有订单终止、所有索赔完结且已完成交易的所有商品和服务质保期均届满后，服务商可向营探发起退还诚信保证金的申请。自终止合作之日起满**三十（30）个**工作日，若服务商符合审批条件（如申请的材料齐全、信息完整无误、签字盖章完整、无未结算完的结算单等），营探将依据协议及相关规则等扣除应扣除部分后，将诚信保证金余额退还至服务商在营探备案的结算账户或服务商名下的其他账户。

**6.2**因出现本规则4.1中所述的行为或服务商与营探签署平台协议中约定的被营探清退的服务商，诚信保证金在清退时不予退还。

**6.3**诚信保证金退款流程：


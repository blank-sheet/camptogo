#    **营探诚信保证金管理规定**

发布日期：2024年1月14日

生效日期：2024年1月21日

## **第一章 总则**

**1.1**平台设立诚信保证金用于加强营探平台管理，防范交易风险，保障服务商与营探平台用户的合法权益。为规范平台及服务商双方行为，明确诚信保证金的管理办法，特制定本规则文件。

**1.2**诚信保证金是指服务商以 "一商户一诚信保证金" 原则向营探缴纳的，用以保证其商品和服务质量以及其严格按照**《营探服务商入驻协议及授权条款》**及各项平台规则合规经营的专项资金。在服务商出现违规情形时用于向营探用户、向营探平台、向第三方等进行赔付赔偿。赔付赔偿的范围除该权利人的直接损失之外，还应包括赔偿营探平台的直接损失和间接损失。服务商同意营探，依照双方签署的协议及平台规则对该诚信保证金进行处置。

**1.3**诚信保证金可用余额是指诚信保证金总额减已赔付金额，再减已标识的暂扣金额后的金额。

**1.4**本规则适用于营探平台所有服务商。

## **第二章 诚信保证金缴存**

**2.1**服务商应根据营探平台要求缴纳诚信保证金，作为服务商侵权、违约、违规、未履行服务承诺时用于对消费者、营探平台和其他权利人进行赔偿的资金。如出现服务商违反平台规则的情形，平台有权**扣除、没收**该诚信保证金。根据《营探基础保证金阶段性缓缴政策（2024）》的规定，符合该缓缴政策的服务商可以自签订营探平台入驻协议之日起至在营探平台第一次确认商品成团之日止，暂缓缴纳基础保证金。该缓缴政策的具体适用是否延续等相关内容详见《营探基础保证金阶段性缓缴政策（2024）》。

**2.2**服务商须保证其用于缴存诚信保证金的资金来源合法，且服务商对该资金依法享有支配权。若服务商诚信保证金系由他人代为支付的，服务商应保证该代缴人清楚知晓所支付款项的性质、用途等情况且同意代为支付，并保证平台不会受到任何人就服务商诚信保证金主张其享有任何权利或以任何理由要求返还。

**2.3**服务商诚信保证金的缴存采取线上缴存的方式，即服务商主动缴存诚信保证金到营探诚信保证金专项账户；营探不接受现金形式缴存诚信保证金。具体缴存方法如下：

2.3.1服务商需根据平台相关管理办法所核定的诚信保证金金额，通过工商银行“小微e管”产品向平台诚信保证金专项账户主动存入“诚信保证金”。诚信保证金交易手续费由服务商承担，按照工商银行的规定以交易金额的0.3%缴纳。服务商应在诚信保证金存入时将工商银行手续费和诚信保证金金额总额一并支付。

2.3.2针对服务商无工商银行对公账户的情况，平台收到服务商及其被委托人（自然人）的法律文书后，允许被委托人通过其个人账户代服务商向平台存入“诚信保证金”（缴存时被委托人需在“小微e管”备注栏中明示：代XX缴存诚信保证金）。针对部分产品（如公益产品），经审核后，允许不存入诚信保证金。 

2.3.3服务商及其被委托人在缴存诚信保证金时，应登录工商银行APP客户端，仔细查看系统中嵌入的协议文本和相关业务说明。

**2.4**自动付款功能：“小微e管”的自动付款功能在T+90日（自然日，下同）生效，该功能主要适用于合作商存在多产品发布、中高风险类型产品发布、产品经常性、连续性发布等情形。自动付款功能生效当日，商户诚信保证金将自动划转至我司在工商银行设立的诚信保证金专项资金一般账户。

**2.5**跨类目诚信保证金：诚信保证金缴纳采取就高原则，诚信保证金按最高金额的类目缴纳；经营过程中增加的类目对应的诚信保证金与原有诚信保证金不一致，服务商须在限期内完成补缴。

**2.6**当服务商入驻营探平台后，服务商须根据诚信保证金标准，依《营探基础保证金阶段性缓缴政策（2024）》的相关规定，按照其经营的类目和实际情况**及时、足额缴存**相应诚信保证金。完成诚信保证金缴存手续后，方可进行交易款项结算提现操作。

**2.7**诚信保证金实行专户管理，营探平台不得私自挪作他用。

**2.8**服务商理解并同意放弃对于所缴存的诚信保证金利息收取的权益，营探无需向服务商支付任何孳息。

**2.9** 诚信保证金缴存产生的全部手续费由服务商自行承担，具体手续费的金额依据营探合作的负责诚信保证金缴存的银行标准进行确定。

**2.10** 诚信保证金的具体缴存方式、流程及注意事项，以营探合作负责诚信保证金缴存的金融机构的规定为准。


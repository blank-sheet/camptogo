（2）若收到上述邮件时，服务商发布的商品和服务质保期未满，则在质保期满后下一个工作日起的**三十（30）个工作日**内完成与服务商的对账、核算结算并退还诚信保证金。

## **第七章 诚信保证金缴纳记录及查询方式**

**7.1** 服务商可登录缴存诚信保证金的银行账户，或通过登陆营探工作台查询诚信保证金相关信息。

7.1.1已缴纳诚信保证金：指服务商已经缴纳的诚信保证金的金额。

7.1.2最大应缴诚信保证金：指根据服务商商品和服务所属类目、业务经营情况等，并根据就高原则所对应的服务商应缴诚信保证金档位。服务商缴纳最大应缴诚信保证金后，方可对现在产生的收入进行结算提现操作。


<essayTitleH3>15.温泉</essayTitleH3>

（1）皮肤过敏者、患有心脏病、高血压者不宜泡温泉。孕妇及手术过后者不宜泡温泉。糖尿病患者不宜泡温泉、因为水温过高可让患者注射的胰岛素吸收加快，而且长时间身体过热使机体能量消耗增加，心脏负担加重，很容易出现意外。容易失眠的人，不要长时间泡温泉。经期及经期前后女性不宜泡温泉。

（2）避免空腹、饭后、酒后泡温泉，泡温泉与吃饭时间至少间隔 1 小时。

（3）进池之前把身上的金属饰品摘下，避免被流黄泡成黑色的。

（4）选择适应自身的高、中、低温度的温泉池，一般从低温到高温，每次 15-20 分钟即可。温泉不宜长时间浸泡，否则会有胸闷、口渴、头晕等现象。在泉水中感觉口干、胸闷时，就得上池边歇歇，或喝点饮料补充水分。

（5）皮肤干燥者浸泡温泉之后最好立刻抹上滋润乳液，以免肌肤水分大量流失引起不适。

（6）过烫过酸的温泉不要泡，温度在 30-45℃比较适宜。

（7）最好不要独自一个人泡，以免发生意外。

（8）泡完温泉之后不必再用清水冲洗，但是强酸性温泉和硫化氢温泉刺激性较大，最好还是再进行冲洗，以防有副作用，皮肤容易过敏的人更需注意。根据水质划分，温泉一般分为中性碳酸泉、碱性碳酸氢钠泉、盐泉和硫磺泉四种，泡温泉前，最好先了解温泉的种类，并根据自身条件进行选择，可避免给身体带来伤害。

（9）泡温泉后，人体水分迅速蒸发，要喝水补充。

<essayTitleH3>16.野外探险</essayTitleH3>

（1）提前了解天气情况：了解要去区域的天气情况，对所要去的地方充分了解，做好各种应急的准备。

（2）了解同行人是否志同道合，是否有丰富的相关经验。要量力而行，不要轻易登上海拔超过 6000 米的高度。

（3）旅游装备用品应根据实际需要选择携带。特定探险旅游环境中的用品必须有极高性能指标，如洞穴探险、攀登雪山、考察原始森林等。要带足常用药品，要有相应的急救药品，掌握一定的急救方法。

（4）意外急救：

①避免雷击：野外活动中存在遭受雷击的危险性。但是通过采取科学有效的措施可以显著地降低这种危险性

a) 预知打雷和雷击。先看到乱积云变大，不久即变成雷云，赶紧到安全地方躲一躲。收音机中有刺耳的杂音、忽下大粒雨滴也是打雷的预兆。

b) 跑向低地。

c) 远离高树或密叶树林。

d) 远离铁塔，去除身上的金属物，装入塑料袋中。

e) 如在水域活动，要赶紧上岸。

f) 不要聚集在一起，应分散开。

g) 小屋内、汽车内、岩背阴处或凹处也是很好的躲避之处，但注意不要靠墙。

②应对落石、雪崩：由高处掉落的一块小石头有时也会严重伤人，甚至致人死亡。因此在山间行走时，一定要注意是否有落石标志，要仔细观察，分辨浮石，一般讲在多石头的地方，浮石的颜色比周围石头新；通过易发生落石区域时，应戴好安全帽或用厚衣服蒙住头，快速通过；尽量提早发现落石，及时避让，避免意外伤害；行走中不小心踏落石头时，要立刻喊出声，通知下面的同伴。 雪崩的威胁非常大。因此，进行野外生存生活训练时，首先要向当地住户打听哪些地方有潜在危险，以避开经常发生雪崩的地区。由地貌特征也能判断雪崩常发地区，如山坡上有雪崩大槽，山坡上方有悬浮的冰川，山脊上有雪檐等。雪崩前，有雪块、冰片落下，这时要确认冰落的方向，然后再决定逃离方向。一旦来不及逃脱而卷入雪崩，手脚要快速像游泳一样运动，尽量使头部浮在雪上，同时抛出身上携带的一些物品作为标识物，以便别人知道你被雪埋的具体位置，及时营救。

③溺水：在江河湖海中遇到复杂水情而无法驾驭时，千万不要慌张着急，想办法让自己浮在水面上，保持浮姿，任水冲流，并注意水波流向，再一点一点由水平方向往岸边移动。在拯救溺水者时，首先考虑用竹竿、树枝、绳索拖拉，或者用大木头、塑料桶等能很好地浮于水面的物体作为浮具实施间接救护，实在无法解决问题了才入水施行直接救护。如果被救上岸的溺水者神志不清，就要采取急救措施，施行心肺复苏术。

④迷路：在山野，尤其是在深山密林中行走时，稍不留神就会发生迷路，这时要保持沉着冷静，然后采取适当的措施：

a) 回到认识的地方。平时在行进的休息间歇要多注意周围的风景和标志，一旦迷失方向，最好回到自己认识的地方，用罗盘和地图确定所处的位置及目的地方位，重新开始行走。折返时不要直走下坡路，因为下坡路视野小，方向不易确认，这是很危险的。

b) 做好山路标志。在山野行进时要注意曾经走过的人留下的用塑料袋、树枝或石头做的记号。走在前面开路的人，遇到特殊状况时，要做标志通知后面的人。c) 如果迷路时天色已晚或从山崖落下受伤，动弹不得，无法按照预定时间到达目的地，这时应做深呼吸，保持镇静，不要贸然离开，在原地露宿，减少体力消耗，同时想办法发出求救信号静待救援。

⑤毒蛇咬伤：在野外如被毒蛇咬伤，患者会出现出血、局部红肿和疼痛等症状，严重时几小时内就会死亡。这时要迅速用布条、手帕、领带等将伤口上部扎紧，以防止蛇毒扩散，然后用消过毒的刀在伤口处划开一个长1 厘米、深 0.5 厘米左右的刀口，用嘴将毒液吸出。如口腔粘膜没有损伤，其消化液可起到中和作用，所以不必担心中毒。被昆虫叮咬或蜇伤时：用凉水冷敷，将刺拔出后再涂抹氨水或牛奶。

⑥骨折或脱臼：用夹板固定后再用冰冷敷。从大树或岩石上摔下来伤到脊椎时，将患者放在平坦而坚固的担架上固定，不让身子晃动，然后送往医院。

⑦食物中毒：吃了腐败变质的食物，除会腹痛、腹泻外，还伴有发烧和衰弱等症状，应多喝些饮料或盐水，也可采取催吐的方法将食物吐出来。


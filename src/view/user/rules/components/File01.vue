a) 肌肉受伤要立即休息，用冰敷消肿，并且抬高患处，滑雪场要特别注意被冻伤，冰敷时间要短。一般2天后如痛感不强烈，可以稍微进行简单的活动，考虑配合热敷和按摩，促进血液循环，吸收血肿。5天后逐渐增加对受损肌肉的锻炼，切忌锻炼过度使其二度出血，慢慢地让它的活动范围和延展度恢复正常。

⑧耳朵伤害症状：耳廓部分受到冲撞发生红肿，或者被利器割伤、撕裂。最具有危险性的是耳内流出液体，或者感觉失去听觉，都应该立即送往医院。 


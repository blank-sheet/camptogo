⑦肌肉伤害症状：如果肌肉完全断裂，伤处凹陷下去，而两端却突出，这时候不会觉得很痛，立即休息，经过专业的肌力恢复锻炼，并不需要手术。如果因为拉伤使血液流出积累在黏膜层中，会觉得疼痛难忍，就需要更长的时间恢复了。 


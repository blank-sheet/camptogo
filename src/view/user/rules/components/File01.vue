①擦伤症状与瘀血症状见前述内容。

②骨骼伤害见前述内容。

③肌肉伤害：

a）肌肉拉伤通常不会使人感到剧痛痛，但需要立即休息，经过专业的肌力恢复锻炼就可以恢复。如果因为拉伤出血积累在黏膜层会觉得疼痛难忍，可能需要更长的时间恢复。


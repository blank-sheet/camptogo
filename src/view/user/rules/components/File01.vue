（1）滑草：乘坐时身体应置于滑板内，上身微向前，手抚滑板两侧把手。滑草前要做好准备工作，穿戴专门的滑草用具，如滑草鞋（板）、滑草履带和护肘、护膝等，手拿助滑竿。 滑草时眼睛要自然平视前方，身体前倾，双膝微曲，使重心向前；双肘微曲，双手握住滑竿朝后一点，身体飞速向前，转弯时控制好身体的平衡，向左转，身体向右倾，向右转，身体向“左倾”。 滑草过程中要收颌近胸以防后脑受伤，其次是要四肢放松，僵直的身体更容易受伤。再次是不要硬撑，一旦失去重心，自然摔倒更不易受伤。 初学者首先要克服对坡度、速度的恐惧，培养滑感，要过渡到滑草的中级阶段，最好找教练或是技术好的朋友进行指导。（2）滑沙：滑沙的整个沙面没有固定的滑道，1.2 米以下的儿童禁止乘坐。乘坐时，滑板是没有安全腰带的，主要原因是防止中途翻板游客与滑板绑缚在一起造成更大的伤害。乘坐时脚踩滑板下沿两侧，手扶住滑板中间部分内侧的木把手，身体微向前倾。下滑过程中不要乱动，一旦发生严重的偏板可采取以下两种方法补救：

①滑板向左严重偏离时，可用右手刮右侧沙面，根据偏离情况在纠正时间和力度上不同控制。

②滑板向右严重偏离时，可用左手刮左侧沙面，根据偏离情况在纠正时间和力度上不同控制。


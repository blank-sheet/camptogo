①咬伤：动物的口腔中含有较多病菌，可能还有泥土等污物，被咬的伤口容易出现化脓等感染，严重者可能出现破伤风。建议用生理盐水彻底冲洗伤口，然后用消毒纱布包扎，后续应第一时间去医院注射破伤风，以防发生破伤风。 如在受伤过程中，还被动物拖拽、拉扯等，导致其他部位受伤，如骨折或是出血等，应在冲洗包扎伤口后，拨打 120 急救电话，待医务人员的专业救治。

②踢伤：一般情况下如果皮肤有破溃的话，建议你用碘附消毒，然后 24 小时以后用热毛巾外敷，尽量少活动，慢慢会消肿，如表皮无破损，但是疼痛剧烈，或是被踢伤的伤口破损严重，建议即刻前往医院检查。

③摔伤： 骑动物导致摔伤，受伤的部位最常见的为脖子、腰和腿。如在野外骑动物摔伤，应用木板将受伤的部位紧紧绑住做好固定，如果没有木板等硬物，建议用衣物做好固定，如果是腰部受伤，建议平躺并拨打 120 请求救援，由医务人员进行专业救治。


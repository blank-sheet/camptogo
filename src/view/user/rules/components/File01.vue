⑩如出现的意外情况较严重、请第一时间拨打急救电话 120，快速平稳地送医院救治。在搬运和转送过程中，颈部和躯干不能前屈或扭转，而应使脊柱伸直，绝对禁止一个抬肩一个抬腿的搬法，以免发生或加重截瘫。立即处理危及生命的问题，针对呼吸心搏骤停及致命的外出血，给予心肺复苏，以及恰当的止血方法救治。复合伤要求平仰卧位，保持呼吸道畅通，解开衣领扣。周围血管伤，压迫伤部近心端动脉干骨。直接在伤口上放置厚敷料，绷带加压包扎以不出血和不影响。骨折处理：详细、认真检查，确定骨折部位，对闭合性骨折应用夹板或康能气垫固定以减轻疼痛、防止继发性损伤，对开放性骨折外露断端不要复位，只作消毒敷料创面包扎，疑似或确诊脊柱及骨盆骨折者，应尽量使身体处于同一水平固定于铲担架上搬运，禁止弯腰和抬腿，以免损伤脊髓，造成瘫痪。活动性出血应予加压包扎，上止血带者，注意上带时间。

<essayTitleH3>11.滑翔跳伞</essayTitleH3>

（1） 注意风速的变化：在 4 秒钟或不到 4 秒钟的时间内，风速变化达 8 公里/小时以上，通常对滑翔伞飞行构成不安全因素。此外，风速变化大于失速速度的一半，则也是不安全的。

（2） 注意躲避机械乱流的着陆点：为了避开物体造成的机械乱流，可从物体的高度测出版风着陆距离，它等于风速乘以物体的高度，即：

D （顺风着陆距离） = V （风速） × H （物体高度） 

例：风速为 10 公里/小时，离开 物体的顺风着陆距离应为 10 倍的物体高度。

（3） 注意起飞前的检查工作：检查整个伞衣有无撕裂、刺穿和擦伤情况，并查看是否有拉开或未缝合的情况，特别要注意查看伞衣下表面的前沿部分。检查每棍伞绳的连续性，是否缠结在一起或磨损，清除所有使悬挂绳彼此相互的黏附。检查伞绳与操纵带的连结情况。确保操纵绳与套环安全连接，并自由地通过导向环的检查一下吊带系统是否磨损或开线如果上述几点中有任何一点出现问题，应立即修正，否则必须马上停飞。

（4） 注意飞行场的判断：如飞行场为陌生地形，飞行员需要有当地飞行员或资深飞行员陪同，先示范飞行以熟悉场地与当地气流型态。

（5） 注意气候的判断：飞行员于每个季节从事飞行伞运动，须先认识不同季节所产生不同的气流形态，以确保飞行安全。春夏季时，须提防天气的快速转变，积雨云的靠近与雷击感电的危险。冬季时，须注意东北季风快速行进的锋面所造成的风速瞬间增强。

（6） 注意提防强大的气流：于破碎地形盘旋飞行时，须提防强大的气流或地形风所造成的乱流。突发状况的处置：飞行员对于突发状况的应变，须于平时即做好严密的训练与心理准备，配合自身的反应与飞行器具的使用，规划好所有的应变计划。

（7） 滑翔跳伞发生意外的急救措施：

①如出现的意外情况较严重、请第一时间拨打急救电话120，快速平稳地送医院救治。在搬运和转送过程中，颈部和躯干不能前屈或扭转，而应使脊柱伸直，绝对禁止一个抬肩一个抬腿的搬法，以免发生或加重截瘫。

②立即处理危及生命的问题，针对呼吸心跳骤停及致命的外出血，给予心肺复苏以及恰当的止血方法救治。

③复合伤要求平仰卧位，保持呼吸道畅通，解开衣领扣。

④周围血管伤，压迫伤部近心端动脉干骨。直接在伤口上放置厚敷料，绷带加压包扎以不出血和不影响肢体血循环为宜，常有效。当上述方法无效时可慎用止血带，原则上尽量缩短使用时间，一般以每次不超过 20分钟为宜，做好标记，注明上止血带时间。

⑤骨折处理：详细、认真检查，确定骨折部位，对闭合性骨折应用夹板或康能气垫固定以减轻疼痛、防止继发性损伤，对开放性骨折外露断端不要复位，只作消毒敷料创面包扎，疑似或确诊脊柱及骨盆骨折者，应尽量使身体处于同一水平固定于铲担架上搬运，禁止弯腰和抬腿，以免损伤脊髓，造成瘫痪。活动性出血应予加压包扎，上止血带者，注意上带时间。

<essayTitleH3>12.水面活动</essayTitleH3>

（1） 在参与水上旅游项目之前，按照自身的身体状况和参与项目的安全性来谨慎评估是否适合参加。有心脏病、高血压、发烧感冒、醉酒等，不适宜参加水上活动，如在活动中自己感觉不适，应主动提出并离水上岸。

（2） 建议结伴参与水面游玩项目，如是需要下水的项目，要详细了解器材的操作和前往的活动场地。如是海面游玩项目，出发前应了解天气情况，同时关注当地海滩的公告和警示：红黄相间的旗帜代表海面平静，适宜游玩；黄旗代表海面有风浪，下水存在危险；红旗则表示海面风浪大，严禁下水。请严格遵守海面警示，不要贸然出行游玩。注意活动区域的安全标志、救援设备和救生员的设置地点。

（3） 听从教练或是船员的指示，出海或是其他要求穿救生衣的水面活动，请严格遵守穿救生衣的规定，如活动提供方未提供救生衣，则应当主动要求。

（3） 急救措施：

①抽筋：抽筋就是肌肉强直性的收缩，往往是因为过度疲劳游泳过久或是突然受冷水刺激造成。当发生抽筋时，应立即上岸擦干身体。如在深水处或腿部抽筋剧烈，无法游回岸上时，应沉着镇静、呼人救援，或自己漂浮在水面上，控制抽筋部位，往往经过休息抽筋肌肉可自行缓解。

②手指抽筋：将手握成拳头，然后用力张开，张开后，又迅速握拳，如此反复数次，至缓解为止。

③手掌抽筋：用另一只手掌将抽筋手掌用力压向背侧并作振颤动作。

④手臂抽筋：将手握成拳头并尽量曲肘，然后用力伸开如此反复数次。

⑤小腿或脚趾抽筋：用抽筋小腿与身体成直角并弯曲膝关节，然后用两手抱着小腿，用力向上拉同时用同侧的手掌压在抽筋小腿的膝盖上，帮助小腿升直。 

⑥大腿抽筋：射曲抽筋的大腿与身体呈直角并弯曲膝关节，然后用两手抱着小腿，用力使它贴在大腿上并做振颤动作，随即向前伸直。

⑦腹直肌抽筋：腹直肌抽筋即腹部（胃部）处抽筋，弯曲下肢靠近腹部，用手抱膝，随即向前伸直。

⑧溺水：保持呼吸道通畅，迅速清除溺水者口、鼻中的泥沙、水草等杂物，以保持呼吸道通畅排除呼吸道及腹腔内污液和水。迅速将溺水者置于抢救者屈膝的大腿上，头部向下，随即按压背部迫使呼吸道和胃内的水倒出。一般肺内水分已被吸收，残留不多，因此倒水时间不宜过长，要分秒必争，以免耽误复苏时间。对呼吸、心跳停止的溺水者立即进行心肺复苏。尽快进行口对口的人工呼吸和胸外心脏按压。 心跳停止者应先进行胸外心脏按压。让伤员仰卧，背部垫一块硬板，头低稍后仰，急救者位于伤员一侧，面对伤员，右手掌平放在其胸骨下段，左手放在右手背上，借急救者身体重量缓缓用力，不能用力太猛，以防骨折，将胸骨压下 4 厘米左右，然后松手腕（手不离开胸骨）使胸骨复原，反复有节律地（每分钟 60～80 次）进行，直到心跳恢复为止。不得随意中止抢救，直至医务人员接手为止。

<essayTitleH3>13.漂流</essayTitleH3>

（1）漂流出发时，最好携带一套干净的衣服，以备下船时更换，同时最好携带一双塑料拖鞋，以备在船上穿，漂流时不可现金和携带贵重物品上船，若有翻船或其他意外事情发生，漂流公司和保险公司不会赔偿游客所遗失的现金和物品；若感觉机会难得一定要带相机的话，最好带价值不高的傻瓜机，事先用塑料袋包好。

（2）上船第一件事是仔细阅读漂流须知，听从工作人员的安排，穿好救生衣，找到安全绳；

（3）漂流船通过险滩时要听从工作人员的指挥，不要随便乱动，应紧抓安全绳，收紧双脚，身体向船体中央倾斜；必须全程穿着救生衣，以防不慎落水。漂时不要做危险动作，一般来说，漂流河段都是比较安全的，只要不自作主张随便下船、不互相打闹、不主动去抓水中的漂浮物和岸边的草木石头，漂流筏不会翻。一旦翻船也不要紧张，憋住气，小心不要呛水，因为你身上的救生衣的浮力足以将人托浮在水面上，而船上的同伴应当伸出划桨让落水者攀抓。若落水者距离较远，要想办法上岸或停留在石头的背水面（迎水面水流强且容易被橡皮艇撞到）等待救援。

（4）不得随便下船游泳，即使游泳也应按照船工的意见在平静的水面游，不得远离船体独立行动。

（5）水上运动容易被水浸湿衣物，女士漂流不宜穿得太透或太薄衣服且不宜穿高跟鞋参加漂流。

（6）漂流是一项富有挑战性并带有一定危险性的户外运动，这点不同于一般传统旅游，所以不适合老、弱、病、残、孕等特殊群体游客，以免出现安全事故。

（7）意外急救：

①平静面对急流：应该平静面对急流，用脚避开前面的岩石。向后轻轻斜靠，让桨为自己把握方向。

a) 远离船只：最可怕的是挤在船和岩石之间。因此要远离船，特别是在顺流的一侧。举起桨求救 一把竖直举起的桨标志着告诉别的船只这儿有一个人在船上。

b) 防止体温过低：冰冷的水在不到十分钟的时间内就可耗尽泳者的力气，应特别小心。应对经过远程游泳的人实施针对体温过低和受到冲击的救护。

c) 与岩石碰撞 ：如果你发现不能避开岩石（这种情况确实存在），可以在碰撞前调转船头或让船头撞上岩石。

d) 船头撞上岩石：让船头撞上岩石将立即让船停下，但可使你通过一些旋转来调整航线。

e) 船侧有岩石：如果船侧有岩石，全体船员最好在碰上之前，立即跳到离岩石最近的船侧，船员的重量将会让顺流船绕开岩石，逆流则流得更高。否则，水将升起抵挡逆流。

f) 一个曾撞过的船非常难操作的，因为它含有很多吨的水。第一步应赶快舀水，再尽力将船停在平静的水域。（现代的自排水橡皮艇则无须舀水）。

②沉陷：

（注：现代的自排水船不会沉陷），如果以上与岩石相撞的情况导致沉陷，就应用绳子从岸上寻求帮助。一条这样的船常常被几吨的力量缠绕，但通常有一处没有这么严重。

措施：

a) 用一根粗绳绕成 D 形环，穿过水道（有必要可在前面打个孔），或船后面的船架。可以用一个拉力系统（由蝴蝶状的环或卡宾轮组成）帮助提升。尽力拉起船，离开水域，用船头或船尾的绳帮助拉向岸边。

b) 如果以上所有努力失败，则让人和东西都在岸边排成一条线，等水位改变。在激流探险中避免让船沉陷是最危险的，应牢牢记住每个人的人身安全比让船远离岩石更重要。

③漩涡：

陷在漩涡里的情况下，除非船凭着很大的惯性冲过漩涡，否则卷曲的波浪会撞回到船上而使它停下来，水也会立即灌进舱内，常常让船猛烈地旋转乃至倾斜。一些漩涡甚至可能会掀翻船，当然这并非很常见，因为船会因浸泡而加重。 

措施：

a) 立即进入顺流的水中以避免可能发生的倾覆，用桨或橹划动顺流的水以从漩涡中脱身而出，尽管漩涡表层的水通常都是逆流，其实在其下层及漩涡的旁侧都有水流，万不得已，用岸上的绳子也可把船从漩涡中拖出来。

④倾覆：倾覆是由诸如大的漩涡、波浪、单侧的波涛及障碍（如石头和倒下的枯树等）所引发的。 救护的对象按先后顺序依次是：你自己，船上的其他乘员，装备。

措施：

a) 试着跳开，以避免撞击到障碍上；

b) 如果确定不会陷入船与石头之间的逆流中，应该尽量地浮在水面上；

c) 可上岸避开这一段急流水域；

d) 尽量保持与你的同伴一起行动，如果有人失踪，应检查船下以确定他是否被绳索或衣物缠住（这就是为什么必须确保没有松散的绳套）不要担心装备，首要的是确定每个乘员的安全；

e) 由于从倾覆的船内游向岸边非常困难，你通常会需要其他船只的帮助，这应该在远离急流的平静水面来操作。救援船只逆水接近，捞起倾覆船只的一条缆绳，再把它牵往岸边，其余船只也应该沿途搭救落水者并尽可能快地清点人数。

⑤靠岸：

急流与瀑布是不可避免的，在无人的急流区系上救生绳以帮助船驶过，并在岸上对船保持密切的控制，切记不可将绳索套在你自己身上，在绳上打个结或将绳绕在树上都可以帮助你实现对船的控制，靠岸的时候务必带上所有东西。

<essayTitleH3>14.潜水</essayTitleH3>

（1）饮酒后，患有心脏病、气喘、高血压、中耳炎的人不能从事潜水活动。

（2）与潜水员、同伴之间在潜水的过程中保持沟通。有效的沟通对潜水过程起到了更好的保障。在国外，并非所有的潜水员都是一带一或者一带二的简单模式，一些热门的潜水点经常会出现一个人带十几个人潜水，因此队伍的规则必须严格遵守，即使在水底出现了突发状况也能够及时找到解决的方法途径。

（3）切忌单独行动，不要单独行动去到陌生的海域潜水，因为对海域的不了解，可能会有诸多隐藏的危险存在，如果单独行动很容易出现不可逆转的后果。同时，如果与同伴一起在水底时，突然发现同伴不见的情况下，一般只允许在水底继续等待或者寻找一分钟便需离开。坚守两人同行的原则，要紧跟领队。

（4）切忌随便触摸，当你在海底见到迎面而来的海龟也许会让你想要近距离地接触一下，成群的珊瑚和可爱的热带鱼也会让你好奇地想要伸手触摸，然而这样的行为是不被允许的。破坏了这片海域的环境和和谐，也许下次再来就无法看到这些神奇的物种了。

（5）要穿配救生衣，配重带最后佩戴。不要使用耳塞。要把面罩内的水清除干净。不要做超呼吸的动作。

（6）在完全离水上岸后再去掉蛙鞋、面罩、呼吸管。

（7）意外急救：

①有呼吸的病人：让其躺下并以 8 升/分钟的流量提供自主呼吸式的纯氧；监控 ABC'S（气道、呼吸、循环、休克、脊椎伤害和严重流血）；联系当地紧急医疗服务中心；将病人送到最近的减压仓；了解并记录病人最近 24 小时的活动（如是否有饮酒，吃药或所吃食物）。

②无呼吸的病人：施加人工呼吸和心肺复苏；以 15 升/分钟的流量提供自流式纯氧；监控 ABC'S；联系当地紧急医疗服务中心；将病人送去较大的医院进行抢救；了解并记录病人最近 24 小时的活动（如是否有饮酒，吃药或所吃食物）。③溺水：咳嗽、虚弱或无呼吸；嘴巴周围有白沫；心脏病 急救措施：如果病人清醒，给予 8 升/分钟自主式呼吸纯氧；如果病人不清醒，施与人工呼吸和心肺复苏并提供 15 升/分钟的自流式纯氧；注意防止病人休克；联系紧急医疗服务中心；将病人送去就近医院并持续监控病情，即使病人感觉好些了（二次溺水可能性非常大）。


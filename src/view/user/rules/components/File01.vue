a) 可以通过冰敷止血消炎，观察得，伤处逐渐由紫红转成黄绿色，3 天之后可使用热敷和按摩来减缓疼痛，5至7伤处颜色天慢慢消失，无大碍。

⑤运动摩擦伤症状：表皮经受了长时间摩擦和压力，使得表皮和真皮层之间出现了缝隙，血液供应跟不上，容易造成局部细胞坏死，这时候组织液乘机进入，就形成了水泡。摸上去感觉刺痛，严重的还会有血液充斥在水泡里。 

措施如下：

a) 立即换掉不合脚的鞋子，如果水泡很大，影响正常行动，可以考虑先用杀菌药品给水泡消毒，然后用消毒过的手术刀在水泡突起切开一道小口，用棉纱压出其中的液体，再抹上抗感染的药品，用纱布覆盖。

⑥骨骼伤害症状：发生轻微的骨裂初期可能不被人们重视，但是 2～3 星期后发觉疼痛仍未消失，而软组织的肿胀并不明显，在运动中会觉得疼痛剧烈。 


<essayTitleH3>1.登山活动</essayTitleH3>

（1）衣着：请选择厚度适中、不太粘汗、能够保护皮肤的舒适的服装，选择适合自己，舒适透气的登山鞋。如出汗较多，可以适当减少衣物，但尽量不要脱下大件衣物，以免受寒。

（2）除合适的衣物外，可能还需要准备便于携带的食物、充足的水以及绳索等安全设备。建议随身携带急救药品，例如止血绷带、云南白药等。

（3）登山前，最好向附近居民了解清楚当地的地理环境和天气变化的情况，选择安全的登山路线，并做好标记，防止迷路。

（4）登山时间尽量选择早晨或上午，午后即下山返回驻地。请不要擅自改变登山路线和时间。

（5）登山过程中，请注意把握上山和下山的速度。避免激烈运动造成肌肉拉伤，避免坐在潮湿、冰冷处或风口停留。可以适当休息，并自行按摩腿部、腰部肌肉。

（6）观景过程中尽量不要行进，行进过程中请专注安全问题；

（7）拍照时应当选择安全的地点、角度，尽量不要站在岩石上进行拍摄，如存在必要情形，请提前检查岩石是否风化，并确保自身安全。

（8）请注意防火问题，不吸烟，不使用明火，以免引起山林火灾。

（9）一旦发生风险事件，应当立即开展自救、互救和救援，必要时应及时报案，并立即拨打120联系急救中心。

（10）自救：登山者应当掌握必要的与登山有关的科学知识，在发生风险情况时进行自我保护，并处理伤病急救和使用急救药品。

（11）互救时，需要因地制宜，迅速采取合适措施进行救援。包括对伤病员进行急救处理，包括实施人工呼吸，包扎伤口等，迅速护送伤病员向低海拔处或安全地点下撤，自己无力承担救援工作时，应立即发出求救信号。

（12）组织救援是一种补救措施，在收到求救信号后应立即实施。

（13）损伤骨折

①损伤出血：小范围表皮出血，无需特殊处理；严重出血须立即采取止血措施；对于小型伤口，请用清洁布块直接压在伤口附近，血渗透布块后应反复替换，直到无活动性出血后，用消毒纱布盖好，绷带固定。如为切伤或砍伤且血流不止，一般为属于动脉出血，必须用止血带或加压绷带止血，但请注意，止血带扎紧0 分钟左右之后，应进行适当放松后再次扎紧，避免被捆扎处以下的肢体血液循环中断引发肢体坏死。也可采用清洁纱布或止血棉等止血剂，填塞在伤口处，用加压绷带固定。

②骨折或扭伤：保持清醒休息，不要勉强走动，避免恶化；可用河水、冰、雪等冷却患部，不要按摩；可用木板等代替物夹住患部，用绷带加压、固定；送往医院的过程中，应小心谨慎搬运，可采用两人手椅、单人背负、两人搀扶、担架搬运等方式。如患者脊椎受伤，必须放在平坦坚固的担架上固定后再搬运，且身体不能弯曲，否则可能会造成脊髓损伤而导致瘫痪。

（14）求救信号：SOS（Save Our Soul）是国际通用的求救信号，可以通过地上标记、旗语等方式发出，几乎任何重复三次的行动都象征着正在求援（如三堆火，三股浓烟，三声口哨或火光）。如使用声音或灯光信号，在每组发送三次信号后，间隔1分钟左右后重复。

①烟、火信号：燃放三堆烟、火是国际通行的求救信号。将火堆摆成三角形，间隔相同最佳。在白天，为制造出有良好定位效果的烟雾，可以在火堆中添加树叶、苔藓等植物、塑胶等散发浓烟的材料。在夜间或深绿色的丛林中，亮色浓烟最为醒目；橡胶和汽油黑色烟雾在雪地或沙漠中最醒目，橡胶和汽油可产生黑烟。应随时准备好信号火种，使之保持干燥、易于燃烧，一旦有任何飞行物驶过，就尽快点燃救助。

②地对空信号：在开阔地放置易被空中救援人员观察发现的信号，信号的规格通常以每个长 10米、宽 3 米，各信号之间间隔 3 米为宜。

“I”——指有伤势严重的病人需立即转移或需要医生；

“F”——指需要食物和饮用水；

“II”——指需要药品；

“LL”——指一切都好；

“X”——指不能行动；

“→”——指按这一路线运动。

③光、旗语 ：光信号：利用阳光和反射镜、玻璃、金属铂片等任何明亮的材料即可发出反射信号光。持续的反射将产生长线和圆点，这是摩斯代码的一种。

④旗语信号：右挥动表示需救援，要求先向左长划，再向右短划。

<essayTextWithWeight>7.1 高原游</essayTextWithWeight>

进行高原游之前，应先进行身体检查，及时掌握自己的血压及心、肺功能情况。在此基础上进行适当锻炼，同时保持良好的心态和充足的睡眠。在高原上可能出现急性高原反应，具体症状表现为头痛、头昏、恶心呕吐、心慌气短、胸闷胸痛、失眠、嗜睡、食欲减退、腹胀、手足发麻等。一旦出现急性高原反应，在症状未改善前，应减少活动，卧床休息和补充液体。大部分人经 3 至 10 天症状会逐渐消失。症状不缓解甚至恶化者，应尽快到医院就诊。16 岁以下60 岁以上者、孕妇、精神疾病患者以及患贫血、糖尿病、心脑血管疾病、慢性肺病者不宜进入高原，如有患重感冒，建议治愈后再进入高原。

（1）飞机直接进入高原，起航前一晚保证充足的睡眠，不吃油腻食物，不饮酒。乘汽车或是火车进入，应做好行程计划，每天上升高度控制在 400-600 米，每到一高度进行适当休息。高原游活动不宜过于频繁，应保证充足的睡眠，同时避免过度焦虑。

（2）衣物：高原日照强烈，户外活动时建议佩戴墨镜和太阳帽，擦防晒以抗紫外线，避免损伤皮肤 。高原昼夜温差大，游客应带上足够的防寒衣物，并注意及时加减衣物。

（3）药品：高原地区呼吸系统容易感染，建议携带阿莫西林、罗红霉素等抗生素药物。高原卫生条件有限，容易患肠胃炎，可带上环丙沙星或磺胺类药物。必须准备利尿剂乙酰脞胺（预防和治疗高原反应的主要药物，可以消除阵发性夜间呼吸暂停，提高夜间睡眠质量，减少晨起时的头痛）。

（4）饮食：多食用富含碳水化合物且易消化的食物；多饮水；建议不要进食过多的肉类；不宜过饱；忌饮酒。

（5）戒烟：高原大量抽烟，将加重高原反应。

（8）高原反应：给氧，降低高度。一旦发生高原反应，应立即至无风处休息，若疼痛严重，可服用镇痛剂止痛。仍不能适应，则应降低高度直到患者症状明显减轻。休克现象，应优先处理并拨打急救电话 120，注意失温及其他并发症。

<essayTitleH3>2.攀岩</essayTitleH3>

（1）衣服：选择合适的衣服。

（2）热身：可先在横渡墙上攀爬简单路线 2-3 分钟以促进血液循环，直至身体些微出汗为止，但须以手臂不至硬化为原则。

（3）动作：尽量平衡攀登动作，逐步提升训练强度，避免伤害周围肌腱或组织。 攀爬时每条路线之间亦须有足够的休息时间，建议为5至10分钟。

（4）受伤：请注意各种症状，不要忽视发炎肿痛等情况。不要过度依赖止痛剂。（5）互助：去野外攀登以前，一定要得到正确的指导，不可盲目行动。多与搭档交流：攀岩是一个需要队友协作的项目。如果说话或者喊叫听不见，那么创造其他的交流方式，比如拉动绳子，来给搭档传递明确的信息。同样，攀登之前对交流方式要达成一致，与搭档议定攀登计划。

（6）不做任何没有安全把握的举动。

（7）攀岩事故：

①坠落在地的伤员，应检查伤情，不要搬动摇晃，及时拨打 120。

②确认受伤之程度患部外观是否正常，如肌肤破裂，骨头外露或异常突出，然后根据 RICE 的处理原则：

Rest 休息：停止运动；

Ice 冰敷：降低出血程度；

Compression 包扎：止血；

Elevation抬高：结合情况决定是否抬高患部。

处理后请决定是否请医或送诊。

（8）扭伤：扭伤时立即予以冰敷，即可减轻疼痛程度及消肿，扭伤后三日内避免浸泡热水，以免加速发炎。扭伤后应尽量休息。

（7）骨折：可用冰敷袋、冷却剂或以塑料袋包裹冰块外包毛巾，一次 10~15 分钟，反复 3至4次，中间间隔 3至5 分钟。建议受伤的四天内不要贴（热敷性质的）药膏。如伤势不见好转，应及时到医院照 X 光并听从医嘱。复原后，逐步加强患部的重量训练及柔软度的训练。

<essayTitleH3>3.滑冰</essayTitleH3>

（1）检查：滑冰前检查冰刀与鞋连接是否牢固，鞋带是否结实，冰刀的内刃和外刃是否打磨锋利，固定的螺丝是否全部拧紧。

（2）服装：保暖的同时不要影响运动，且保证汗液正常挥发。

（3）休息：建议初学者每15至30 分钟休息一次，休息时应把鞋带松开，使脚上血液畅通快缓解疲劳。

（4）姿态：站立时两脚略分，与肩同宽，脚尖稍向外，两腿稍曲，上体稍前倾，目视前方。身体重心要通过两脚平稳地压在刀刃上，踝关节不应内或外倒，滑行时俯身、弯腿，重心向前。如果后仰摔倒，应侧身用手撑地，减少冲击，同时避免头部受伤。

（5）意外处理：

①伤口：出现伤口后，应采取措施防止伤口感染；应及时止血，防止出血过多，避免休克。如为头部受伤，应尽快用无菌纱布或洁净布压迫止血（可以根据情况决定是否用手按压约15分钟），若伤口较严重，应立即拨打急救电话 120或送诊。

⑤骨折：骨折往往是由摔伤、撞伤和击伤所致。判断骨折主要看皮下是否出血、畸形、骨擦音、肿胀以及非正常（非关节）运动等现象。如果自己判断可能是骨折了，应立即用夹板固定，无夹板时也可用书本等固定。若受伤较重，应立即拨打急救电话 120送医。

<essayTitleH3>4.滑冰滑雪</essayTitleH3>

（1）不得参与：患有高血压、心脏病、癫痫、精神病患者等患有严重疾病者，醉酒者，孕妇等不适合极限运动的所有人员。

（2）准备：滑雪前必须补充充足热量，但建议不要大量进食后立刻开始活动；在上雪道前做好充分的热身，让身体各关节充分活动开，避免运动伤害；请勿携带坚硬物品，尽量不要佩戴眼镜。

（3）活动：初学者应量力而行，停留休息时，应停在滑雪道边上，休息或重新进入雪道时，应要充分注意并避开从上面滑下来的人，切勿发生碰撞。不要擅自滑出滑雪场界线。

（4）姿态：站在单板上时，注意在摔倒时尽量将身体放低，在感觉身体倾斜要摔倒时，要尽量下蹲身体向后倾，起到缓冲作用。

（5）未成年人：未成年人滑雪必须有成年人全程陪同。

（6）发生事故的紧急处理办法：

①体温过低或冻伤：皮肤冷而且苍白，脉搏缓慢，有时候不规则，产生视觉障碍，看不清楚或者重影，嗜睡或者短暂的无意识都是体温过低的表现。皮肤变白，感觉麻木而且无痛感，均为冻伤的前兆。通常指尖，脚趾头、耳廓、鼻尖、脸颊和下巴这些血管末梢的血液循环最差，会先被冻伤。当感觉局部皮下肿胀，按下去感觉有硬块，泛白之后还发紫，则是深度冻伤。

缓解措施如下：

a）应对体温过低的伤员，应立即转移伤者位置，更换被打湿的衣物，用保暖的羽绒或者太空棉包裹保暖，补充热的甜饮料。千万不要强行鼓励伤者做动作，或者用揉搓的办法升高体温，也不能通过喝酒缓解寒冷感。一旦发现无意识，应立即送往医院抢救。

b）发生冻伤的情况，也要立即离开低温场地，更换打湿了的衣物鞋袜，可以用 40℃的温水浸泡 冻伤部位，用羽绒、太空棉材料保暖。不能用搓揉的方法促进冻伤部位的血液循环，而且浸泡的水温不能过高，切勿马上烤火。

②眼睛伤害症状：程度较轻的雪盲，眼睛会感觉发痒、疼痛，眼球发红，像有沙粒钻进眼睛一样，畏光而且经常流泪。如果有异物进入眼球可能也会发生同样症状。 

措施如下：

a) 切勿用手揉眼，应该拉起上眼睑，检查是否有异物进入眼睛，确认是否存在眼球受伤的情况。如无，可以考虑用纯净水或者眼药水清洗眼球，然后用柔软的医用棉纱覆盖眼睛，闭眼休息。尽量免受各种刺激，减少用眼，戴太阳镜降低视觉亮度，5至7天症状可能会自动消失。

③擦伤和裂伤症状：皮肤表皮受到刮擦，破皮之后渗出血液或者组织液，伤口有灼痛感为擦伤；皮肤受到锋利物体的打击，伤害到皮下的柔软组织裂开口子，伤害到真皮层则为裂伤。

措施如下：

a) 用消毒纱布适当地给伤口施加压力以止血，最好使用硼砂水消毒，如无，可使用生理盐水代替，请在清洗伤口后，涂上消炎软膏或者紫药水，不用纱布覆盖。面部擦伤最好每隔2小时清洗一次，防止感染后留下痕迹。若天气过冷，应该注意保暖，防止伤处冻伤。

b) 裂伤可以用相同的处理办法。如果伤口深及皮下脂肪和肌肉，应立即送往医院进行缝合。

④淤血和血肿症状：血液从血管中渗出，进入到皮肤和黏膜中，会沉淀在皮肤下面，呈现出紫红色，成为瘀血块，使得肌肉组织发炎，受伤部位肿胀，按上去感觉疼痛。血肿则是因为血液从血管渗出，并没有进入到皮肤或者黏膜中，而是堆积在一处，导致肿胀，有时候非常严重而不得不采用抽出部分血瘀的办法来缓解疼痛。 


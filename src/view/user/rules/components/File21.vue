指营探为提供更好的用户体验而开发、增设的可选择性的增值服务项目。增值服务费独立区别于上述平台佣金，它是一种可供服务商自主选择的专项服务内容，不因服务商未选择使用该服务项目而影响其使用营探基础电商平台信息发布业务的功能，服务商选择使用增值服务，可为其带来其他便利服务和优质体验。 具体增值服务项目开通以营探平台相关通知/公告为准。

<essayTextWithWeight>3.2.5 平台提款手续费</essayTextWithWeight>

服务商在平台的交易资金提款，由服务商向营探主动申请。该提款手续费由服务商承担，按笔支付。 提款费率标准按照营探合作银行的收费标准收取，营探不得在交易手续费中赚取居间价差。

银行手续费费率如下：1万元以下（含1万元）每笔收费5元；1万元以上至10万元每笔收费10元；10万元以上至50万元每笔收取15元；50万元以上至100万元每笔收取20元；100万元以上每笔按汇划金额的万分之零点二收取，最高不超过200元。（如有变动按照银行通知为准，参见中国银行电子银行企业服务转账汇款费率表：https://www.boc.cn/ebanking/service/cs1/201309/t20130929_2508179.html）


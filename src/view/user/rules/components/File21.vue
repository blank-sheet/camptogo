<essayTitleH2>第三章 收费细则</essayTitleH2>

<essayTextWithWeight>3.1 收费对象</essayTextWithWeight>

使用营探平台服务，并通过营探平台发布商品和服务的服务商。

<essayTextWithWeight>3.2 一般收费标准</essayTextWithWeight>

以服务商使用营探平台发布的每个商品和服务的约定的平台佣金为基准计费，以每款商品为单位计算其累计平台佣金，进行核算与结算。

<essayTextWithWeight>3.2.1 平台佣金</essayTextWithWeight>

营探平台佣金采用约定结算价方式执行，即单个商品的每单平台佣金为服务商发布商品和服务的未划线价和服务商与营探平台的实际结算价之间的差额，单个商品的每单平台佣金比例不小于上述未划线价的15%（商品客单价低于249元的按照3.2.2条款执行固定保底服务费），该平台佣金不受商户自行提供的补贴、优惠而影响，非经双方协商一致不得变更。

<essayTextWithWeight>3.2.2 保底服务费</essayTextWithWeight>

当服务商发布商品和服务的未划线价小于等于 249 元人民币时，平台收取保底服务费，费用标准为 40 元/人次。收取保底服务费的商品不适用3.2.1。

<essayTextWithWeight>3.2.3 收费说明及要求</essayTextWithWeight>

平台佣金是按照消费者通过营探平台报名交易后按照实际发生收取。平台交易采用交易成功后置收取平台佣金模式，为确保电商法顺利实施并保护交易公平，避免平台向服务商提供推广等服务后无法获得合理收益，服务商须给予营探符合市场情况的优势商品结算价格（详见入驻协议），并允许营探在规定范围内向消费者提供促销优惠方案；同时，服务商通过营探发布的商品零售价格须不高于该商品在其他渠道的历史最低销售价格(包括线下线上渠道价格、官方及第三方的销售等价格)。

计算方法：营探平台佣金=【服务商发布商品和服务的未划线价（非优惠促销后的实际成交价格）-服务商与营探平台约定的结算价格】 x 交易成功订单数


1.5.5品类券：只针对某一类别商品和服务使用的优惠券；

1.5.6单品券：只针对某一个指定商品和服务使用的优惠券；

1.5.7平台会员优惠券：是成为营探会员身份可领取使用的优惠券。

## **第二章** **如何获得优惠券**

**2.1**优惠券采用系统自动发放和消费者主动领取两种形式。

**2.2**消费者主动领取的优惠券可以通过商品和服务详情页、活动促销页面、我的礼券等页面进行领取。

**2.3**消费者可能通过邀请、注册、抽奖、积分兑换、满赠等线上推广形式获得优惠券，参加活动并满足活动促销要求可获得优惠券，活动信息请持续关注营探各活动信息及广告内容。

**2.4** 服务商发放优惠券的，由服务商**100%**承担费用；平台发放优惠券的，费用根据当时的联合促销政策按约定比例承担。

**2.5** 优惠券的使用规则（如发放时效、适用范围等），服务商可通过服务商工作台进行设置，商品发布生效后即按照相关设置履行。


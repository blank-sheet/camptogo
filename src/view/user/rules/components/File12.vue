## **第一章** **什么是优惠券**

**1.1**营探 (www.camptogo.com) 优惠券 (以下简称优惠券)是营探联合服务商对广大消费者的一种优惠和回馈。为使更多的消费者享受到这种优惠，特制定**《营探优惠券使用规则》**（以下简称本规则）。如果您接受并使用营探优惠券，那么您便接受了本规则。

**1.2**优惠券仅在消费者通过营探平台为个人消费而发生购买行为时使用，不适用于团体购买、企业顾客购买、渠道客户购买及其他非以个人消费为目的的购买行为。

**1.3**  同一手机、同一IP地址、同一或高度相似收货地址、同一设备号、同一ID、同一收件人或其他可以合理显示为同一账户的情形，其中一项或数项符合均视为同一账户。

**1.4**违规注册的账户不得使用优惠券。以非正常手段注册、一人注册多个账户，或使用他人手机号、地址等信息注册账户等情形均视为违规注册。一经发现，平台有权限制或取消用户参与活动的资格，有权取消奖励、追讨用户已领取的奖励（或同等价值的现金）。平台有权对违规用户采取限制登录、封禁账号、限制提现等措施，并保留追究用户其他法律责任的权利。

**1.5** 优惠券的使用受商品和服务类型、订单金额等限制，订单必须符合优惠券的使用条件才可使用，优惠券分为以下几类：

1.5.1满减券：用来直接减免部分订单金额的优惠券；


**5.12**由于账户被盗用等非常规原因或消费者自身原因造成不能使用优惠券的情况，与营探无关。

**5.13**使用优惠券并已支付成功的订单，若消费者在活动开营前申请退款：购买单个商品和服务的优惠，按购买单品时实际支付金额退款；购买多个商品和服务的优惠，按被取消的商品和服务售价所占订单金额比例拆分实际支付金额进行退款，未取消的商品和服务仍按支付时的优惠比例享受优惠。已使用优惠券不退还。


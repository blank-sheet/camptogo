# **营探优惠券使用规则**

发布日期：2024年1月14日

生效日期：2024年1月21日

## **第一章** **什么是优惠券**

**1.1**营探（www.camptogo.com）优惠券（以下简称优惠券）是营探联合服务商对广大消费者的一种优惠和回馈。为使更多的消费者享受到这种优惠，特制定**《营探优惠券使用规则》**（以下简称本规则）。如果您接受并使用营探优惠券，那么您便接受了本规则。

**1.2**优惠券仅在营探消费者为个人消费而发生购买行为时使用，不适用于团体购买、企业顾客购买及其他非以个人消费为目的的购买行为，使用他人手机号、地址等信息注册，或以非正常手段注册、一人注册多个账户等视为违规注册，同一手机、同一IP地址、同一或高度相似收货地址、同一设备号、同一ID、同一收件人或其他可以合理显示为同一账户的情形，其中任意一项或数项符合均视为同一账户。

**1.3**优惠券的使用受订单金额和商品和服务的限制，订单必须符合优惠券的使用条件才可使用，优惠券分为以下几类：

1.3.1满减券：用来直接减免部分订单金额的优惠券；

1.3.2打折券：通过折扣的形式进行订单金额减免的优惠券；

1.3.3店铺券：仅在某一服务商提供的商品和服务中可使用的优惠券；

1.3.4平台通用券：凡是参与营探全场平台抵扣的商品和服务均可以使用的优惠券；

1.3.5品类券：只针对某一类别商品和服务使用的优惠券；

1.3.6单品券：只针对某一个指定商品和服务使用的优惠券；

1.3.7平台会员优惠券：是成为营探会员身份可领取使用的优惠券。

## **第二章** **如何获得优惠券**

**2.1**优惠券采用系统自动发放和消费者主动领取两种形式。

**2.2**消费者主动领取的优惠券可以通过商品和服务详情页、活动促销页面、礼券中心等页面进行领取。

**2.3**消费者可能通过邀请、注册、抽奖、积分兑换、满赠、线上活动、推广活动、购买赠与等形式获得优惠券，参加活动并满足活动促销要求可获得优惠券，活动信息请持续关注营探各活动信息及广告内容。

**2.4**服务商发放优惠券的，由服务商100%承担费用；平台发放优惠券的，费用根据当时的联合促销政策按约定比例承担。

**2.5**优惠券的设置、发放与适用范围，服务商可通过工作台进行设置，商品发布生效后即按照相关设置履行。

## **第三章** **如何查看优惠券**

**3.1**进入“我的－礼券中心”，在此可查看您拥有的所有待使用、已使用、已过期的优惠券。


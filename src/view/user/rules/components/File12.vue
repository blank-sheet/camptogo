**5.1**优惠券仅限于使用营探APP/小程序展示的对象产品，并且仅在APP/小程序订购时使用。

**5.2**优惠券仅抵扣商品价格，不适用服务费、税费、运费及其他费用。

**5.3**优惠券不能兑换现金。

**5.4**每张优惠券仅能使用一次，不记名、不挂失、不找零、不退换。

**5.5**使用优惠券抵扣部分的金额不开具发票。

**5.6**优惠券应在券面标示或系统显示的有效期内使用，过期作废，不能延期使用。

**5.7**消费者同意按照具体优惠券规则使用优惠券。

**5.8**一般情况下，一张订单仅限使用一张优惠券，不可拆分；特殊情况，多个优惠券可叠加使用的，详情见优惠券具体说明。


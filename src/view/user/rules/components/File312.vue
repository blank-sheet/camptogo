# 灵活就业服务合作协议


## **特别声明：**

**为免疑义，本协议项下的新就业形态劳动者仅限于从事生产经营活动的个人或本协议项下的个体工商户，与其名下注册的其他个体工商户没有任何关系，同时该新就业形态劳动者与甲方（含关联方）不存在劳动关系、劳务关系或其他类似的劳动人事法律关系，且不是甲方（含关联方）的法定代表人、股东、董事、监事等人员。**

**一经发现违反上述规定行为的，乙方有权取消甲方或新就业形态劳动者使用云账户服务的资格，云账户有权要求该新就业形态劳动者立刻注销云账户根据与其签订的《个体工商户登记管理合作协议》代其注册的个体工商户，并有权上报和配合相关国家机关，同时保留追究责任主体法律责任的权利。**

 

**鉴于：**甲乙双方均为依据中国法律设立且有效存续的企业或其他组织。乙方享有云账户平台的合法使用权，且具备相应的资源和资质可为甲方业务开展提供灵活就业总分包服务。甲乙双方依据《中华人民共和国民法典》及相关法律法规的规定，本着平等、自愿、诚信的原则，在平等互利的基础上友好协商并签署本协议。

 

## **第一条** **定义**

1、**关联方：**指一方直接或者间接持有另一方的股份总和达到25%以上；双方直接或者间接同为第三方所持有的股份达到25%以上。如果一方通过中间方对另一方间接持有股份，只要其对中间方持股比例达到25%以上，则其对另一方的持股比例按照中间方对另一方的持股比例计算；两个以上具有夫妻、直系血亲、兄弟姐妹以及其他抚养、赡养关系的自然人共同持股同一企业，在判定关联关系时持股比例合并计算。

2、**集团：**指符合本协议“关联方”定义、具有关联关系的企业与云账户同时展开合作时，为享受本协议附件一中所列的集团计费规则，自主选择合并而成的企业联合体。

3、**云账户：**指与甲方开展灵活就业总分包服务合作并签署合作协议的乙方及其关联方的统称。

4、**云账户平台**：指云账户开发的App、网站、客户端或其他网络服务系统，包括但不限于津智云商商事登记管理系统、云账户综合服务平台、云账户App及云账户助手微信小程序。

5、**个体工商户：**指乙方根据与从事生产经营活动的个人签订的《个体工商户登记管理合作协议》协助其进行个体工商户登记并经乙方主管市场监督管理局核准，注册成立、通过云账户平台从事合法合规生产经营活动的个体工商户（不包括前述个人通过其他途径登记注册的个体工商户）。

6、**新就业形态劳动者：指自我雇佣、自我管理、自担成本、自负盈亏并通过云账户平台从事合法合规生产经营活动的个人或本协议项下的个体工商户。**

7、**生产经营收入：**指新就业形态劳动者通过与云账户合作，承接云账户所分包业务进而从事生产经营活动而取得的收入。

8、**业务服务费：**指乙方基于甲方业务需求为甲方提供灵活就业总分包服务所收取的费用，包含基础服务费和加成服务费两部分，各类费用的具体核算方式见本协议附件一。

 


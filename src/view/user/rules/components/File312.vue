4、乙方承诺其向甲方提供的与本协议履行相关的业务数据等资料是真实、合法、有效的，不存在任何虚假、伪造或舞弊的行为，甲方有权对乙方提供的有关业务数据进行审核。

5、乙方承诺不得从事涉嫌违反相关法律法规及公序良俗（包括但不限于涉嫌传销、赌博、暴力、淫秽、恐怖行为、洗钱、偷税、漏税、逃税、非法集资、诈骗）等影响本协议执行或可能损害甲方权益的行为。

6、乙方保证甲方履行本协议项下合作事项的过程中使用云账户平台不会侵犯其他方的知识产权或其他合法权利。

7、乙方承诺将为甲方使用云账户平台服务提供及时、充分、有效的安全保障或技术防护措施或服务（包括但不限于提供系统使用培训、系统升级/加固、技术保障或网络安全防护措施），确保甲方业务账号使用、操作环境的安全、可靠、稳定。

 

## **第七条 不可抗力**

1、“不可抗力”是指双方不能控制、不可预见或即使预见亦无法避免的事件，且该事件足以妨碍、影响或延误任意一方履行其全部或部分义务，该事件包括但不限于政府行为、自然灾害、突发公共卫生事件、战争、计算机病毒、黑客攻击或电信机构服务中断造成的事件。

2、本协议任意一方因不可抗力不能部分或全部履行协议的，不承担违约责任，但如在迟延履行后发生不可抗力的，不能免除责任。 

3、由于不可抗力导致本协议无法继续履行或不可抗力持续30个自然日仍未停止的，双方可经协商一致终止本协议。此情形下，双方应当友好协商并尽力配合对方完成协议终止事宜。

 

## **第八条 违约责任**

1、除本协议另有约定外，本协议任意一方不履行或不完全履行本协议约定的义务或承诺即构成违约。守约方在向违约方出具书面违约提醒，要求违约方继续履行协议或采取必要的经济补救措施后的10个自然日内（含），未收到违约方书面回应的，守约方有权解除本协议。违约方应当赔偿其违约行为给守约方造成的实际损失，包括但不限于合理的律师费、诉讼费、公证费。

2、出现下列情形之一的，甲方可以单方解除本协议，并有权要求乙方承担相应违约责任、赔偿相应损失：

2.1乙方未按本协议约定向甲方开具相关增值税发票，且经甲方书面通知后10个自然日（含）仍未纠正的；

2.2乙方宣传过失，给甲方造成严重负面影响，致使甲方社会评价严重下降的；

2.3其他乙方拒绝或未能履行本协议所约定的其应履行的职责、义务，且经甲方书面通知后10个自然日（含）仍未纠正的。

3、出现下列情形之一的，乙方可以单方解除本协议，并有权要求甲方承担相应违约责任、赔偿相应损失：

3.1甲方从事的业务活动涉嫌违反相关法律法规及公序良俗的（包括但不限于涉嫌传销、赌博、暴力、淫秽、恐怖行为、洗钱、偷税、漏税、逃税、非法集资、诈骗）或者严重损害乙方利益的；

3.2甲方未能及时支付业务服务费，且经乙方书面通知后10个自然日（含）仍未纠正的；

3.3甲方宣传过失，给乙方造成严重负面影响，致使乙方社会评价严重下降的；

3.4甲方直接、间接或诱使其他第三方从事与乙方有竞争关系的业务或擅自将服务转服务（转售或者二次销售）于第三方的；

3.5甲方具有本协议项下违约或涉嫌违法行为导致乙方行使中止履行权，且经乙方书面通知后10个自然日（含）仍未纠正的；

3.6甲方存在本协议第五条第4款中关于乙方合作的新就业形态劳动者范畴之约定情形的；

3.7其他甲方拒绝或未能履行本协议所约定的其应履行的职责、义务，且经乙方书面通知后10个自然日（含）仍未纠正的。

4、本协议任意一方出现包括但不限于工商注销、申请破产、经营异常且不能合理解释或通过合理方式无法联系、进入清算程序或被取消营业资格，另一方有权以书面形式通知对方终止本协议且无需承担任何违约责任。因政府机关不再授予或调整协议任意一方相应经营资质或权利等，导致本协议部分条款或全部条款无法履行的，遭受该行为的一方不承担违约责任。

 

## **第九条 保密责任**

本协议任意一方应对本协议所涉及的任何内容以及双方在执行本协议过程中相关的一切法律、商业、合作业务的所有信息进行保密。未经对方允许，均不得向本协议约定之外的任意第三方披露（但前述保密信息已经公开、在签署本协议之前已通过其他合法来源获悉或应司法机关、行政机关要求进行披露的除外）。本协议解除或终止后，本条约定的保密责任仍然有效。

 

## **第十条** **个人信息保护**

1、个人信息处理：甲乙双方承诺并确认，各自为实现本协议合作目的所处理的新就业形态劳动者的个人信息已符合《中华人民共和国个人信息保护法》及相关法律法规的要求；对于涉及个人敏感信息处理的，已通过合法有效的方式经过权利人的同意，以确保取得个人信息处理的合法性基础。


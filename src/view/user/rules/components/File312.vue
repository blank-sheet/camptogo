3、个人信息主体权利请求共同配合：被请求方应及时通知另一方共同处理合法的个人信息权利请求，包括但不限于访问权、更正权、删除权、撤回同意权、可携带权和账户注销权等，并共同配合新就业形态劳动者行使权利。

4、个人信息保护违约责任：任意一方违反本协议或适用《中华人民共和国个人信息保护法》相关规定的，或造成共同处理的个人信息的丢失、泄露、损坏或销毁的，应当承担相应的法律责任；对另一方造成损失的，应当承担相应的法律责任。

 


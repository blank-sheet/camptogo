## 第五章  产品 广告竞价排名

为充分实现广告公平和有效性的投放，先对“营探”客户端中投放的广告进行实时自动竞价，按价高者得的原则确定广告位的实际分配。为规范竞价行为，现制定本规则，以资共同遵守。

<essayTextWithWeight>5.1</essayTextWithWeight>竞价要素

5.1.1每千人展示成本（简称“CPM”（Cost Per Miller））：CPM指广告每向用户展示一千次，商户需向营探支付的费用。这里的“点击”指用户实际点击广告链接的动作。

5.1.2每次点击成本（简称“CPC”（Cost Per Click））：CPC指用户每点击一次广告，商户需要向营探支付的费用。这里的“点击”指用户实际点击广告链接的动作。在竞价模式下，竞价方的CPM报价与CPM结算价并不一致,CPC报价与CPC结算价并不一致。

5.1.3点击到达率 (简称“CTR”(Click-Through-Rate)) : CTR 是广告的点击数量与展示数量的比例。

CTR=竞价广告的点击数+竞价广告的展示数X 100%

5.1.4 每一千次展示可以获得的广告收入(简称“ECPM”(effective cost per mille)) :

在竞价方以 CPM方式竞价的情况下: ECPM-CPM报价

在竞价方以CPC方式竞价的情况干ECPM以竟价广告的1000次展示为基数，按照1000次展示中用户的点击数和竞价方的CPC报价来计算。

具体计算公式为: ECPIMI-CTR×CPC×1000

<essayTextWithWeight>5.2</essayTextWithWeight>竞价排名规则

5.2.1 CPM 报价/CPC报价

竞价方的CPN报价(币种为人民币)以

(1)小图：10元为基础价。

(2)大團: 15元为基础价。

在上述基础价上按照0.01元的整数倍增加。

即CPM报价-基础价(10 元或15元) +0.01 元XN (N为整数)

5.2.2 竞价方的CPC报价(币种为人民币)以

(1)小图: 0.3元为基础价。

(2)大圍: 0.45元为基础价。

在上述基础价上按照0. 01元的整数倍增加。

即CPC报价-基础价(0.3 元或0.45元) +0.01元×N(N为整数)

5.2.3 CPM 报价/CPC报价必须以信函或电子邮件等书面形式确认。竞价方变更CPM报价/CPC报价的，也同样需以信函或电子邮件等书面形式确认。广告发布方在收到确认后的24小时以内将新的CPM报价/CPC报价输入竞价系统。竞价方以口头形式发出或变更CPM报价/CPC报价的，务必及时以书面形式确认。否则，由此引起的延误应当由竞价方自行负责。

5.2.4竞价方的CPM报价/CPC报价必须符合本规则5.1.1条或5.1.2条的要求，否则，将被广告发布方视为无效报价。竞价系统将仍按照竞价方最新的有效报价执行。若竞价方没有有效报价的，竞价系统将推定竞价方的报价为基础价

(即CPM报价为: 10 元或15元; CPC 报价为: 0.3 元或0.45元)。

5.2.5 某一竞价广告的展示数量在1000次以内(含本数)时，CTR统一设定为1.6%， 超过1000次以后，由竞价系统按照实际点击数量和展示数量计算。

5.2.6竞价成功: 当某一用户使用“营探”客户端并刷新页面时，针对该页面中首先出现的空闲广告位，所有的竞价广告均按照竞价系统实时计算的ECPM值从高到低排位，排名第一的竞价广告可以获得在该广告位的展示，这就被视为该竞价广告竞价成功。

竞价成功后，在该用户的“营探”客户端中，该竞价广告于48小时内不再参与竞价。当同一用户在该“营探”客户端中再次刷新页面，使得下一空闲广告位出现时，其余竞价广告再次按照上述规则竞价。

5.2.7 CPM 结算价/CPC结算价

当某一竞价广告竞价成功时， 竞价系统按照当时排名第二位的竞价广告的ECPM值，自动增加0. 01元，以此为基础计算CPM结算价/CPC结算价。

即CPM结算价/CPC结算价的计算公式为:

CPM结算价= (排名第二位竞价广告的ECPM+0.01元)

CPC结算价= (排名第二位竞价广告的ECPM+0. 01元)÷1000÷CTR

当 CPM结算价小于基础价(10 元或15元)时，竞价系统自动取值为基础价(10 元或15元)即:

基础价(10元或15元)≤CPM结算价

当CPC结算价小于基础价(0.3元或0.45元)时，竞价系统自动取值为基础价(0.3 元或0.45元)，当CPC结算价大于CPC报价时，竞价系统自动取值为CPC报价。即:

基础价(0.3 元或0. 45元)≤CPC结算价≤CPC报价

